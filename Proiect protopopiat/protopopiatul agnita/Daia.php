            <h6 style="line-height: 1.2";>A. Istoricul comunității parohiale</h6>

             <p class="col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
           <span style=" text-decoration:underline;">Numele localității</span>
           </p>

           <p class=" text-justify "
            style="text-align:justify;
              text-indent: 4.9ch;
           "
            >
              Satul Daia, este unul dintre cele șase sate ce compun comuna Roșia, județul Sibiu, aflat în imediata apropiere a municipiului Sibiu la cca 10 km de acesta, între Sibiu și comuna Roșia. Este prima localitate situată pe drumul ce leagă Sibiul de orașul Agnita, zonă cunoscută sub numele de Valea Hârtibaciului. Vatra satului Daia se desfășoară pe o vale depresionară adâncită de după Dealul Dăii, deal ce separă localitatea de municipiul Sibiu, loc de unde derivă numele satului din germanul „dalie”, „delle”, cu semnificaţia adâncitură. Istoricii care au făcut mențiuni despre localitate spun că regele Geza al II-lea(1141-1162) al Ungariei a chemat coloniști din regiunea de nord-vest a Germaniei. Cancelaria de la Budapesta a scris despre ei numindu-i saxoni (sași). Alți istorici spun că locuitorii satului Daia ar fi urmașii unor cruciati blocați în Serbia. La întoarcerea din cruciadă s-ar fi refugiat și ar fi rămas în Transilvania, acestia fiindțăranioriginaridinValoniașiFlandra.
           </p>


             <p class="col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
           <span style=" text-decoration:underline;">Prezentarea generală a localităţii (geografic, istorico-demografic, archeologic, cultural, economic)</span>
           </p>

            <p class=" text-justify "
            style="text-align:justify;
              text-indent: 4.9ch;
           "
            >
              La începutul sec al XIII-leac âteva familii din Villa Hermanni (Hermannstadt) întemeiază gospodării țărănești noi pe Valea Hârtibaciului. Aceste gospodării au avut nevoie de o zonă protejată în care să se poată refugia din cauza numeroaselor atacuri ale turcilor sau tătarilor. Familiile de sași stabilite aici au construit încă de la începutul sec al XIII-lea un zid de fortificații în interiorul cetății fiind ridicată o bazilică de tip romanic (1270), iar turnul clopotniță în forma lui actuală datează de la începutul sec al XVII-lea.Prima mențiune documentară despre sat apare în anul 1327 când preotul Peter de Thalheim este menționat ca făcând parte dintr-o instanță de judecată din Hermannstadt. Perioada medievală este legată și în satul Daia de evenimentele istorice ce s-au petrecut în provincia Transilvania, fiind dominată în această zonă de spiritualitatea și cultura săsească, românii făcându-și simțită prezența pe aceste meleaguri abia spre sfârșitul sec al XVI-lea.
           </p>

            <p class=" col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
              Prima biserica a românilor din satul Daia a fost din lemn, o bisericuță ridicată în jurul anului 1858 pe locul actualei săli culturale(,,sala de pomeni”) iar în anul 1863 s-a construit casa parohială, ambele pe terenul aparținând cultului greco-catolic, dar cu sprijinul și contribuția tuturor dăienilor. În acest locaș de cult se desfășurau toate slujbele, abia mai târziu românii din satul Daia au putut sa-și construiască cele două biserici din sat, cea greco-catolicăși cea ortodoxă. Cele două biserici au fost construite în aceeași perioadă la doi ani una după cealaltă, 1936 biserica cu hramul „Buna Vestire” și 1938 biserica cu hramul „Sf. Arhangheli Mihail și Gavriil”, de către meșteri locali cu ajutorul credincioșilor.După 1948 când cele două biserici au rămas la Biserica Ortodoxă s-a slujit alternativ în ele credincioșii ortodocși asigurând atât întreținerea lor cât și a casei parohiale.Astfel s-au facut de-a lungul timpului mai multe reparații interioare și exterioare și s-au introdus utilități, gaz metan în 1956 și curentul electric în 1966.Demn de menționat este faptul că în anul 1927 un număr de peste 60 de familii (96 pers.) de confesiune greco-catolică s-au întors la Biserica Ortodoxă fiind păstoriți în acei ani de preoții ortodocși din Roșia, Ioan Binda și Ioan Tudor, după puțin timp fiind numit Protosingelul Evghenie Brânzescu care a păstorit până în anul 1941.
           </p>

            <br>
            <h6 style=" text-align:justify; line-height: 1.2";>B. Istoricul bisericii parohiale</h6>


           <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              <span style=" text-decoration:underline;">Istoria zidirii ei:</span>
           </p>

            <p style="text-align:justify; text-indent: 5ch;">
              Biserica ortodoxă actuală în care se slujește de mai bine de 77 de ani a fost zidită în anul 1938 pe terenul atribuit în urma reformei agrare din anul 1924 prin decizia nr. 1075/1930 iar fondurile pentru construirea bisericii au fost colectate de la credincioșii din întreaga țară alături de un important ajutor dat de Mitropolitul Ardealului, Nicolae Bălan și de fostul prefect al județului Sibiu, Nicolae Regman. Planul bisericii a fost făcut de arhitectul Cernea din Sibiu iar construcția a fost realizată de meșterul zidar Andrasch împreună cu meșterul dulgher Stoia din Roșia, aceștia fiind ajutați pe tot parcursul lucrărilor de credincioșii ortodocși din sat.
            </p>

             <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              Începând cu anul 2009 credincioșii ortodocși din satul Daia au hotărât construirea unei noi biserici mai mari și mai încăpătoare care să răspundă cu adevărat nevoilor actuale ale parohiei. Datorită faptului că în anul 2009 a fost retrocedată biserica în care s-a slujit mai bine de 50 de ani cultului greco-catolic, credincioșii ortodocși majoritari, cca 600 de suflete s-au văzut nevoiți să-si desfășoare activitatea în cealaltă biserică și în care s-a slujit mai puțin și care se afla într-o stare avansată de degradare, aceasta fiind mai mică și neîncăpătoare. Construirea unei noi biserici mai mari a fost un deziderat mai vechi al credincioșilor ortodocși din satul Daia. Acest demers s-a materializat la 29 august 2010 la praznicul „Tăierea capului Sfântului Ioan Botezătorul” odată cu oficierea Slujbei pentru punerea pietrei de temelie de către Înaltpreasfințitul Dr. Laurențiu Streza Mitropolitul Ardealului împreună cu un important sobor de preoți și diaconi, în prezența unor oficialități locale, alături de numeroși credincioși din parohie și nu numai. Atunci s-a consacrat această lucrare de construire a viitoarei biserici cu hramul „Înălțarea Sfintei Cruci”.
            </p>

            <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              După stabilirea locului unde urma să se construiască a urmat elaborarea proiectului de arhitectură realizat de către Arh. Nicolae Tulban din orașul Săcele, jud Brașov. Documentația a fost analizată de catre D-na Arh. Gruncă Ecaterina, care a emis Autorizatia de construire în primavara anului 2011. Lucrările de construire au demarat în luna mai a anului 2011, cu sprijinul deosebit al S.C. Euro Wagen S.R.L. Bradu prin d-nul Dan Ciprian.
            </p>

            <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              În luna iunie 2012 au început lucrările de zidire cu cărămidă concomitent cu armarea și turnarea stâlpilor de rezistență și a celor decorativi și cofrarea, armarea și turnarea cafasului care înconjoară pronaosul.După terminarea zidurilor s-a realizat centura de rezistență de la cota 5,2m urmată de cofrarea, armarea și turnarea bolților semicilindrice de peste lateralele cafasului și plăcile peste proscomidiar și diaconicon.S-a continuat apoi zidirea și turnarea stâlpilor până la cota 8m unde s-a turnat cea de-a doua centură de rezistență care susține bolțile și semicupolele absidelor. Aceste bolți și semicupole s-au cofrat, armat și turnat în toamna anului 2012, ajungând până la arcele de sub inelul turnului inclusive, lucrările încheindu-se în luna decembrie din cauza iernii.
            </p>

            <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              În anul următor 2013 s-au reluat lucrarile de la stadiul unde au rămas din anul precedent finalizându-se lucrarile de zidire la turn și cupola. Apoi s-au achiziționat lemnul pentru acoperiș aprox. 60 mc de material lemnos din rășinoase profilat(grinzi, dulapi, scândură etc) și s-a trecut la partea de execuție a șarpantei. Aceste lucrări au fost destul de dificile datorită faptului că acoperișul urmează formele bolților și cupolelor acestea având forme rotunjite. În paralel cu acoperișul s-a făcut și izolarea termică fonică și hidrofugă a bolților și cupolei cu vată bazaltică de 15cm și folie anticondens iar peste acestea a fost montată asteriala. Tot în anul 2013 s-a achiziționat și tabla de inox titanizat împreună cu crucile de pe biserică de la o firmă de profil din Ucraina după care s-a contractat de către Consiliul Parohial executantul acoperișului. Lucrările la învelitoarea de tablă s-au desfășurat în mai multe etape din septembrie până în decembrie când s-au finalizat.În paralel cu această lucrare s-a început și tencuirea bisericii atât în interior cât și la exterior lucrare care a continuat și în 2014 și se află în desfășurare. Trebuie menționat faptul că tot în anul 2104 s-au executat și montat tâmplăria din pvc cu geam termopan triplu stratificat și cu geam decorativ de către firma Kaufmann Industries din Deva și s-a realizat ușa principal de intrare în biserică din stejar sculptat.
            </p>

             <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              Toate aceste lucrări s-au realizat cu binecuvântarea Înaltpreasfințitului Dr. Laurențiu Streza Mitropolitul Ardealului și cu sprijinul deosebit al Secretariatului de Stat pentru Culte, al Consiliului Județean Sibiu prin Arhiepiscopia Sibiu, a unor firme și sponsori dintre care amintim S.C. Trans-Agape S.R.L., Primaria Comunei Roșia, Primaria Râul Sadului, Primaria Sadu și cu contribuția nemijlocită a credincioșilor ortodocși din Daia și mulți alții. Printre donatorii cei mai importanți se numără D-na Maurer Silvia, fam. David Nicolae alături de Pr. Dr. Ian de Maere un mare om de cultură cu reședința în satul nostru, care ne-au sprijinit cu sume importante de bani pentru construirea bisericii. În anul 2011 ca urmare a unei frumoase colaborări cu credincioșii stabiliți în ultimii ani în satul nostru într-o zonă rezidențială numită Daia Nouă, la initiativa domnului Prof. Dr. Mițariu Mircea care a și sponsorizat aproape întreaga lucrare am construit un paraclis (altar de vară) . Acesta a fost construit pe un teren pus la dispoziție de Primăria Roșia, de către niște meșteri populari maramureșeni din Poienile Izei după stilul traditional maramureșan al construcțiilor de lemn. După această prezentare generală a lucrărilor desfășurate în ultimii ani putem spune că lucrările la noua biserică credincioșilor ortodocși din Daia se află într-un stadiu destul de avansat aceștia nadăjduind în ajutorul lui Dumnezeu și contribuind cu toate mijloacele materiale și spirituale pentru mutarea cât mai grabnică în noua biserică.Pentru această mutare mai sunt foarte multe lucrări de realizat cum ar fi finalizarea tencuielii interioare cu pregătirea pentru pictură, sistemul de încălzire centrală, mobilarea altarului și a bisericii etc.Demn de menționat este faptul că în anul 2011 ca urmare a unei frumoase colaborări cu credincioșii stabiliți în ultimii ani în satul nostru într-o zonă rezidențială numită Daia Nouă, la initiativa domnului Prof. Dr. Mițariu Mircea care a și sponsorizat aproape întreaga lucrare am construit un paraclis (altar de vară) . Acesta a fost construit pe un teren pus la dispoziție de Primăria Roșia, de către niște meșteri populari maramureșeni din Poienile Izei după stilul traditional maramureșan al construcțiilor de lemn.
            </p>


              <p style="text-align:justify; text-indent: 5ch;">
              <span style=" text-decoration:underline;">Arhitectura (plan, dimensiuni, material de construcţie):</span>
           </p>

             <p style="text-align:justify; text-indent: 5ch;">
              Arhitectura bisericii este una simplă în formă de navă zidită din cărămidă și lemn, formată din patru încăperi, pridvor, pronaos, naos și altar cu catapeteasma zidită. Deasupra pridvorului se înalță turnul clopotniță urmatpeste pronaos și naos de o boltă semicilindrică încheiată deasupra altarului de o arcadă semicirculară de beton armat care sustine și acoperișul din lemn. Deși s-au facut mai multe reparații de-a lungul timpului totuși starea actualei biserici s-a degradat foarte mult ajungând să-i fie afectată structura de rezistență, repararea acesteia fiind aproape imposibil de realizat.
            </p>





