          <p style="text-indent: 5ch;">
            Numărul de credincioşi: în prezent parohia este formată din 140 familii din care 96 familii şi 94 văduvi.
            Activităţi pastoral-misionare, culturale, editorial, filantropice, catehetice ş.a.: cu ajutorul consilierilor şi comitetului parohial, biserica desfăşoară tot timpul lucrări de întrajutorare a familiilor nevoiaşe, menţine o legătură permanentă cu şcoala pentru formarea tinerilor de mâine cu o ridicată ţinută morală şi patriotic.
