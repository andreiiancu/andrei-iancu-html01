            <h6 style="line-height: 1.2";>A. Istoricul comunității parohiale</h6>



          <p class=" text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
           <span>Numele localităţii:</span>  de-a lungul timpului localitatea este menţionată cu mai multe denumiri:, Bürgesch Berjes, Berjesch, Börd’eš, Bürgisch, Burgesch, Schlierlingsdorf, Bürkösch, Bürkös. Filologia maghiară interpretează numirea de Bârghiş ca provenind din Bürkös – cucută. În vechime terenul era plin de cucută, aşezat mai la vale de cum este situat actualmente.
           </p>

             <p class=" text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
           <span style=" text-decoration:underline;">Prezentarea generală a localităţii (geografic, istorico-demografic, archeologic, cultural, economic)</span>
           </p>

              <p class=" text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
            Localitatea este aşezată la 10 km de oraşul Agnita şi la 50 km de municipiul Sibiu. Teritoriul comunei Bârghiş a fost locuit din cele mai vechi timpuri, existând diferite urme de aşezări omeneşti încă din neolitic. şi epoca timpurie.
            După perioada tulbure a năvălirii popoarelor migratoare, perioadă în care ştirile sunt inexistente, apariţia şi dezvoltarea feudalismului în Transilvania a adus prefaceri adânci în societatea acestei ţări, care se împarte din ce în ce mai mult şi mai evident în clase şi în pături cu interese contradictorii. Pe harta marelui principat al Transilvaniei făcută de Spiridon Petti şi tipărită la Sibiu în anul 1862, localitatea Bîrghiş, figurează sub numirea de Birghişi.
            Prima menţiune documentară a localităţii Bârghiş datează din 1357, următoarele menţiuni fiind în 1400, 1405, 1428, 1505, 1641, 1642, 1677, 1745, 1760, 1848.
            Din punct de vedere administrativ, localitatea Bîrghiş a fost situată pe teritoriul comitatului Alba, care comitat a fost împărţit de Maria Tereza în Comitatul Alba de Sus şi Alba de Jos.
            Situaţia ţăranilor români de pe teritoriul scaunelor săseşti, era ceva mai bună decât a celor iobagi din comitate. Ei se bucurau de libertate personală, dar şi asupra lor apăsau obligaţii arbitrare, căci în afară de dijmele datorate administraţiei, se percepeau zeciuiala pentru clerul săsesc şi taxele de întreţinere a şcolilor săseşti.
            Viaţa culturală a românilor din Bârghiş a avut mult de suferit datorită politicii practicată de fostul imperiu austro-ungar faţă de naţiunea română. Datorită condiţiilor social-economice un număr destul de mare de români ai comunei noastre au fost nevoiţi să părăsească locul natal în vederea găsirii unor locuri de muncă mai bune.
           </p>

            <br>
            <h6 style=" text-align:justify; line-height: 1.2";>B. Istoricul bisericii parohiale</h6>
          <p style="text-indent: 5ch;
              ">
              Românii ortodocşi din Bârghiş au opus rezistenţă puternică față de  unirea cu Biserica Romei. Preoţii uniţi au fost bătuţi şi nu au fost lăsaţi să intre în biserica ortodoxă pe care au preluat-o abuziv.
            </p>

           <p style="text-align:justify; text-indent: 5ch;">
              <span style=" text-decoration:underline;">Istoria zidirii ei:</span>
           </p>

            <p style="text-align:justify; text-indent: 5ch;">
              Biserica ortodoxă română <span style="color:red; font-weight:500"><i>Sf.Cuvioasă Paraschiva</i></span> a fost zidită în anul 1872, având hramul „Sfânta Cuvioasă Paraschiva”. Costul lucrărilor a fost de 700 florini austrieci. Cheltuielile pentru construcţia bisericii au provenit din contribuţia credincioşilor a 15 clăi de grâu, timp de 5 ani, dat lui Leib Liechenschtein (român curat, din părinți evrei), pentru avansarea sumei de 700 de florini austrieci, conform contractului din arhiva parohială.
              Planul de zidire a Bisericii a fost aprobat de Consistoriul Arhidiecezan în şedinţa din 4 mai 1867. Lucrarea a fost executată de Iohan Fleischer, meşter din comuna Moardăş.
              Arhitectura (plan, dimensiuni, material de construcţie):
              Biserica este zidită din piatră, în formă de corabie, fără boltă, cu turn obişnuit în Ardeal. Ulterior biserica a fost împodobită în interior cu icoane pictate în ulei pe placaj executate de pictorul Roth în anul 1942. Pardoseala bisericii este din scândură. O primă restaurarea interioară şi exterioară s-a făcut în anii 1942-1962 şi a costat 10.000 lei.
           </p>

            <p style="text-align:justify; text-indent: 5ch;">
              Biserica are două clopote din anii 1922, confecţionate la Timişoara, cu cheltuiala românilor din America. Menţionăm că în timpul războiului din 1914-1918 a fost rechiziţionat clopotul cel mare de 70 kg.
              Antimisul cu litere chirilice din anul 1751, semnătura fiind ştearsă , a fost înlocuit de-a lungul anilor cu antimise purtând semnătura ierarhilor în funcţie, iar astăzi Sfânta Liturghie se săvârşeşte pe sfântul antimis purtând semnătura Î.P.S. Dr. Laurenţiu Streza.
           </p>

           <p style="text-align:justify; text-indent: 5ch;">
              <span style=" text-decoration:underline;">Pictura (când a fost realizată, de către cine, când a fost restaurată):</span>
           </p>


            <p style="text-align:justify; text-indent: 5ch;">
              În anul 1982, sub păstorirea preotului Nicolae Piroş s-a zugrăvit interiorul şi exteriorul bisericii de zugravul Simion Hîrţoagă din Agnita, costând 20.000 lei. În anul 1988 prin voinţa întregii parohii s-a început pregătirea pentru lucrarea de pictură în tehnica fresco. Lucrarea a fost aprobată de Arhiepiscopia Ortodoxă Română Sibiu la data de 21.01.1988 cu nr. 202, iar de Comisia de pictură bisericească Bucureşti la 01 februarie 1988 pe seama zugravului bisericesc Nicolae Govoreanu din Blaj. S-a înlăturat vechea tencuială de pe toată suprafaţa interioară a Bisericii, aplicându-se alta nouă, din var şi nisip, iar fresca aplicată pe pereţi este din var şi fuior de cânepă. Lucrarea de pictură a fost executată în patru ani, iar costul a fost de 370.000 lei. Pregătirea pereţilor a costat 20.000 lei, iar materialele 18.000 lei.
           </p>

            <p style="text-align:justify; text-indent: 5ch;">
              În luna ianuarie 1989 s-a încheiat contractul pentru confecţionarea întregului mobilier din biserică cu sculptorul în lemn Nicolae Găman din localitatea Lunca, judeţul Neamţ. Lucrarea s-a terminat în luna decembrie 1990. Cele două candelabre din sticlă cu 12 şi 6 braţe au fost executate într-un atelier de confecţionat candelabre din Bucureşti de arhitectul Popescu Constantin.
              Plăţile pentru întreaga lucrare fost făcute numai din contribuţia benevolă a credincioşilor din parohie, precum şi a fiilor satului. După finalizarea acestor lucrări, biserica a fost sfinţită la 1 noiembrie 1992 de către Dr. Antonie Plămădeală, mitropolitul Ardealului, Crişanei şi Maramureşului. Până la această dată biserica nu a fost sfinţită de Arhiereu.
           </p>

            <p style="text-align:justify; text-indent: 5ch; font-weight:200; text-decoration: underline; color:#ff3232">
              A doua biserică din parohia Bârghiş, cu hramul „Sfântul Mucenic Gheorghe”, a fost zidită în 1824.
           </p>

            <p style="text-align:justify; text-indent: 5ch;">
              Costul lucrărilor a fost suportat din contribuţia credincioşilor.Biserica este zidită din piatră şi cărămidă, în formă de corabie, cu turnul obişnuit din Ardeal. Este împodobită în interior cu icoane pictate pe placaj de pictorul Roth, aduse de la Biserica I. Pardoseala este din scândură. Restaurarea exterioară şi interioară s-a făcut în anul 1920, când s-a reparat peretele dinspre nord, care se dărâmase. De asemenea în anul 1942 s-a făcut o reparaţie generală. Biserica are un clopot mic din anul 1801, confecţionat în oraşul Sighişoara. Sfântul antimis de aici este semnat de Î.P.S. Dr. Nicolae Bălan în anul 1948. Altul mai vechi nu se află şi nici altul mai nou. Preoţii slujitori au fost: Nicolae Fărcaş, Alexandru Pop, Victor Suciu, Filip Crişan, Petru Tîrziu, Nicolae Piroş, Horia Stanciu. Biserica nu este sfinţită de Arhiereu. Biserica fiind foarte mică şi fără utilităţi, în ea nu se slujeşte decât ocazional şi pe timp de vară.
           </p>

            <p class="col-lg-12"> <span class="font-italic" >Obiecte vechi de cult:</span>  Nu este cazul.</p></p>
             <p class="col-lg-12"> <span class="font-italic" >Manuscrise şi cărţi vechi:</span> Nu este cazul.</p>

             <p class="col-lg-12"> <span class="font-italic" >Pomelnice şi alte înscrieri sau inscripţii:</span>  Nu se cunosc.</p>

             <p class="col-lg-12"> <span class="font-italic" >Cântăreţi şi epitropi de seamă:</span>  Vrednici de pomenire sunt şi epitropii Seban Şerba, Duşleag Traian, Roman Nicolae precum şi cântăreţii Blaga Simion, Mădăraş Nicolae şi Sârbu Aurel.</p>
