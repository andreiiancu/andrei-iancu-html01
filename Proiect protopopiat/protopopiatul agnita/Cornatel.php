          <h6 style="line-height: 1.2";>D. Activități culturale și filantropice în trecut:</h6>

           <p class="col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
           <span style=" text-decoration:underline;">Activitatea culturală</span>
           </p>
          <p class="col-lg-12" style="text-indent: 5ch;">
            Între manifestările culturale ale localităţii amintim umblatul cu colinda de Crăciun a grupurilor de copii, pe timpul zilei, şi a grupurilor de tineri, pe timpul nopţii, împărţiţi în două cete, „susuieni” şi „josoieni”. La praznicul Bobotezei avea loc „ieşirea la Hârtibaci”. Întreaga comunitate ieşea de Rusalii la troiţe, unde se săvârşea slujba sfinţirii apei împreună cu rugăciunile pentru hotar. În anul 1979 a avut loc manifestarea culturală „Fii satului”, fără însă a se mai fi repetat până în prezent. În biserica parohială există o fotografie în care este surprins evenimentul.
          </p>

