            <h6 style="line-height: 1.2";>A. Istoricul comunității parohiale</h6>

             <p class="col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
           <span style=" text-decoration:underline;">Numele localității</span>
           </p>

           <p class=" text-justify "
            style="text-align:justify;
              text-indent: 4.9ch;
           "
            >
            Localitatea Cornăţel, din punct de vedere administrativ, are statut de sat aparținător comunei Roşia, județul Sibiu. A făcut parte din Protopopiatul Sibiu, până la 01 martie 2017, când a trecut sub jurisdicția Protopopiatului  Agnita.
           </p>


             <p class="col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
           <span style=" text-decoration:underline;">Prezentarea generală a localităţii (geografic, istorico-demografic, archeologic, cultural, economic)</span>
           </p>

            <p class=" text-justify "
            style="text-align:justify;
              text-indent: 4.9ch;
           "
            >
              Parohia Ortodoxă Română Cornățel este așezată pe malul stâng al râului Hârtibaciu, în partea dreaptă a șoselei asfaltate și a fostei căi ferate care leagă Sibiul de Agnita și Sighișoara, aflându-se la o distanță de 18 km de Sibiu.
              Este învecinată la est cu localitățile Nucet  și Săcădate; la sud cu Cașolț și Daia ; la vest cu Roșia Sibiului, iar la nord cu localitatea Hosman. Populația satului este formată din români și câteva familii de romi.
           </p>

            <p class=" col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
              În vremi trecute de demult în localitate s-au stabilit și patru familii de maghiari veniți probabil din părțile Târgului Secuiesc. Aceștia au trăit într-o atmosferă perfectă cu ceilalți locuitori ai satului, chiar încuscrindu-se cu ei, respectând atât sărbătorile lor, cât și ale noastre statisfăcându-și nevoile religioase prin Biserica Ortodoxă din localitate.
              Date istorice precise despre localitatea Cornățel nu se cunosc , dar prin cuvintele unor oameni în vârstă (profesorul Nicolae Benchea şi învățătorul Petru Popa- decedaţi ) se știe că în secolul XVI s-au stabilit pe vatra actualei localităţi 70 de familii refugiate din jurul Severinului. Pe plan local se aflau câteva familii de iobagi români adunați de prin împrejurimi. Se crede că dintre cei aflați aici ar fi fost ruși de origine, dar romanizaţi. Împărăteasa Maria Tereza a adus aici câteva familii de maghiari din părțile Târgului Secuiesc spre a-i folosi ca slujbași si oameni de încredere, ei facând legătura între grofii stăpânitori şi iobagii români. Putem aminti nume ca: Grofu, Telechi, Cente (Chente).
           </p>


           <p class=" col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
              Ocupația locuitorilor viețuitori pe aceste plaiuri a fost agricultura practicată pe pământul fertil ce se află în perimetrul geografic al localităţii.
              Paralel oamenii s-au ocupat şi cu creșterea animalelor, ocupație care, mai ales după primul război mondial, a luat un avânt deosebit, cornățenii devenind mari negustori de vite. Acest fapt le-a creat posibilitatea de a cumpăra pământ, dar a dus oarecum si la slăbirea dragostei față de pământ, de a-l lucra cu răbdare, așa cum moșii și strămoșii lor au lucrat ca iobagi.
           </p>

            <p class="col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
           <span style=" text-decoration:underline;">Referitor la toponimie</span>
           </p>

           <p class=" col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
              Localitatea este menționată cu mai multe denumiri de-a lungul timpului: Cornățel ( Hîrtibav, Cornățăl, Hârtibaciu, Harwesterf, Harbachsdorf, Harwesdorf, Harbach, Harendorf, Harrebach, Haresdorf, Hortobagyfalva ).
           </p>

           <p class=" col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
              În sat, în acest moment, există un punct de asistență medicală și o Şcoală Primară cu grădiniță și clase de studiu I-IV.
              De asemenea avem un local, fost cămin cultural, în care locuitorii îl închiriază la anumite evenimente: nunti, înmormântări, parastase, boteze.
              Viețuitorii băștinași din Cornățel, dintru început au fost ortodocși, coloniștii veniți din nordul Transilvaniei au fost greco-catolici reveniți la Ortodoxie. Astăzi toți sunt membrii activi ai Bisericii Ortodoxe.
              Localitatea Cornățel este atestată documentar încă la anul 1306 sub numele de Cornachel, fiind vorba de transcrierea numelui românesc Cornățel, diminutiv al cuvântului cornet, însemnând pădure de corni și mărăcini ce cresc la noi.
           </p>

            <br>
            <h6 style=" text-align:justify; line-height: 1.2";>B. Istoricul bisericii parohiale</h6>


           <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              <span style=" text-decoration:underline;">Istoria zidirii ei:</span>
           </p>

            <p style="text-align:justify; text-indent: 5ch;">
              Nu se cunoaște cu precizie începutul vieții religioase în parohia CORNĂȚEL. La anul 1792 exista actuala biserică, ceea ce ne oferă posibilitatea să credem că o viață religioasă organizată a existat înainte de această dată.
              Edificiul păstrează un valoros ansamblu de picture murale și de icoane, datând de la finele veacului al XVIII-lea și începutul celui următor.
              Un fost veteran al satului N. Dobrotă, decedat la vârsta de 97 de ani spune că știe „nu din văzute”, ci de la bunicul său, că pe locul actualei biserici era o clădire acoperită cu paie în care se țineau adunările iobagilor, atunci când li se dădeau porunci și în care se țineau și slujbele religioase atunci când venea preotul. Aceasta ar fi fost înainte de anul 1700. Biserica Ortodoxă actuală și-a conservat aspectul original, față de alte biserici în care, în urma unor restaurări acesta a fost stricat. Într-o conscripție din anul 1733 se face pomenire de existența unei biserici în Cornățel. În Cornățel rezistența față de uniație a fost foarte puternică neacceptând preoți uniți.
            </p>

             <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              Într-un document publicat la Blaj în 21 martie 1760 se menționează că în Cornățel existau 6 familii de uniți fără nici un preot și 99 de familii (495 suflete) păstorite de un preot neunit (ortodox).
              Biserica de zid din Cornățel cu hramul: „Intrarea Maicii Domnului în Biserică” s-a realizat în urma inițiativei obștii sătești nefiind cunoscute nume de ctitori, nici data târnosirii. Tradiția orală consemnează anul 1792, momentul în care biserica era construită.
              După emiterea edictului de toleranță religioasă din 1781, sătenii din Cornățel s-au preocupat de înlocuirea vechii biserici de lemn cu una din zid, care există și azi. În anii 2006, s-a reușit o zugrăvire în interiorul bisericii, o reparație generală exterioară, înlocuirea țiglei de pe turn și corpul bisericii și procurarea de cărți de cult.
              În anul 2015, biserica parohială a fost introdusă în rândul monumentelor istorice.
            </p>

              <p style="text-align:justify; text-indent: 5ch;">
              <span style=" text-decoration:underline;">Arhitectura (plan, dimensiuni, material de construcţie):</span>
           </p>

             <p style="text-align:justify; text-indent: 5ch;">
              Biserica din Cornățel are formă simplă de corabie având ca arhitectură îmbinarea a trei stiluri: romanic, bizantin și gotic. La interior este simplă având cupolă pe centru pe care se păstrează încă pictura originală, executată se pare de frații Grecu din Săsăuș. Biserica de mici dimensiuni, este construită proporționat, prezentând un plan rectangular cu structura mononavă cu compartimentare ritual de tip oriental: naos 2/3 și pronaos 1/3.
              În extremitatea vestică a navei a fost atașat un turn (clopotniță) cu uși de acces dispuse pe laturi (miazăzi și miazănoapte), ușa de vest fiind zidită de-a lungul timpului.
              În anul 1899 cu ocazia unor reparații capitale în interiorul bisericii s-a introdus podișorul pentru tineret.
              Ca materiale de construcție s-au folosit: piatră în fundație, cărămidă pentru ziduri, care au o grosime de peste o jumătate de metru, țiglă pentru acoperiș.
            </p>

            <p style="text-align:justify; text-indent: 5ch;">
              În timpul păstoririi preotului Ioan Aron (1849-1882 ) s-au strâns fonduri pentru construirea Casei Parohiale și a Şcolii Confesionale. În anul 1866 a cumpărat în vecinătatea bisericii curtea lui Ion Dobrotă pe care s-a clădit școala, astăzi cămin cultural.
            </p>

             <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              În anul 1891 preotul Ioan Petrișor a construit, în imediata vecinătate a școlii, casa parohială, care există şi azi.
            </p>


             <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              În anul 1907 preotul Gheorghe Sîmpăcean a cumpărat, pe seama parohiei, trei clopote, dintre care două, în timpul primului război mondial, au fost luate de armatele germane.
            </p>


             <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              În ceea ce privește pictura interioară a bisericii din Cornățel putem aminti pe Ioan Grigorievici zugravul, al cărui nume e înscris cu litere de-o șchioapă și disimulat în interiorul sulului cu fragmentul de text citit din proorocia lui Isaia. Această lucrare de pictură s-a făcut după anul 1781.
            </p>

             <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              A doua etapă în decorarea cu pictură murală a bisericii s-a încheiat după cum atestă inscripția de la baza pandantivului dinspre sud-est la anul 1820. „Cu cheltuiala satului fiind și cu ajutoriu Ione Dobrotă, Toma Danciu judele, Iosif Jurca, Popa Toma 1820”.
              Un alt text existent este: „Cu ajutorul … acu acest sfânt lucru cu toată cheltuiala robilor lui Dumnezeu, anume: Nicolae Vasii, Iosiv Pinciu din Daie, Vasile Morar ctitori întru pomenire veșnică”.
            </p>

              <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
                Prin analiza stilului și a organizării temelor iconografice este evident că imaginile pictate aparțin unui zugrav din familia Grecu. Părintele Ioan Fulea amintește că decorul zugrăvit la Cornățel aparține meșterilor zugravi Grecu: Gheorghe și Alexandru.
                La Cornățel s-a păstrat și s-a conservat forma originală a picturii de la începutul veacului al XIX-lea.
            </p>

            <p class="col-lg-12"> <span class="font-italic" >Obiecte vechi de cult:</span> Nu sunt în proprietatea parohiei.</p>

             <p class="col-lg-12"> <span class="font-italic" >Manuscrise şi cărţi vechi:</span> În patrimoniul parohial se găseşte o evanghelie tipărită cu binecuvântarea mitropolitului Andrei Baron de Șaguna, în litere chirilice, la anul 1859.</p>

             <p class="col-lg-12"> <span class="font-italic" >Pomelnice şi alte înscrieri sau inscripţii:</span> În biserica parohială există două inscripţii de la anul 1820. Prima dintre ele, situată la baza pandantivului dinspre sud-est, datată la anul 1820, conţine următorul text: „Cu cheltuiala satului fiind și cu ajutoriu Ione Dobrota, Toma Danciu judele,  Iosif Jurca, Popa Toma 1820”. Cea de-a doua, situată pe peretele nordic al bisericii, conţine următorul text: „Cu ajutorul… au acest sfânt lucru cu toată cheltuiala robilor lui Dumnezeu, anume: Nicolae Vasii, Iosiv Pinciu din Daie, Vasile Morar ctitori întru pomenire veșnică”.</p>

             <p class="col-lg-12"> <span class="font-italic" >Cântăreţi şi epitropi de seamă: </span>Din rândul cântăreților bisericești care au deservit biserica din Cornățel putem aminti: Ioan Căliman, Traian Popa, Virgil Căliman şi Emil Mihu. Din rândul epitropilor bisericii din Cornățel putem aminti: Mihu Mihu, Nicolae Coman, Gheorghe Nistor, Aurel Pintea, Ioan Rociu, Nicolae Nan, Petru Aldea, și actualmente Ilie Părău.</p>

          <br>

            <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              <span style=" text-decoration:underline;">Şirul preoţilor parohi:</span>
           </p>

            <p class="col-lg-12" style=" text-align:justify; text-indent: 5ch;">
