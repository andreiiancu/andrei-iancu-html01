          <h6 class="col-lg-12" >C. Cimitirul</h6>

            <p class="col-lg-12" style="text-indent: 5ch;">
              Biserica deţine două cimitire. Cel dintâi, situat în jurul bisericii parohiale, nefuncţional, unde se află monumente ale eroilor satului căzuţi pe câmpurile de luptă în primul şi al doilea război mondial, precum şi câteva morminte vechi. Cel de-al doilea, situat la marginea satului, este funcţional, acolo fiind aşezaţi la loc de odihnă răposaţii satului.
            </p>

