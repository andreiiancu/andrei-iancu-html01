            <h6 style="line-height: 1.2";>A. Istoricul comunității parohiale</h6>



          <p class=" text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
           <span>Numele localităţii:</span>  Brădeni. Toponimia localităţii a suferit numeroase modificări de-a lungul timpului: 1349 – Villa Hegun, 1350 - Villa Hendorf, 1353 - Heghig, 1385 - Hegen, 1476 - Heghen, 1504 - Heendorf, 1886 - Henndorf. Ultimul nume a fost păstrat până în anul 1968 când, datorită organizării administrative a teritoriului, apare cu numele de Brădeni (nume ales datorită unei păduri de brad aflată în apropiere), denumire păstrată până astăzi.
           </p>

             <p class=" text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
           <span style=" text-decoration:underline;">Prezentarea generală a localităţii (geografic, istorico-demografic, archeologic, cultural, economic)</span>
           </p>

              <p class=" text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
              comuna Brădeni este aşezată pe un râu al Văii Hârtibaciului înconjurată din toate părţile de dealuri acoperite cu păduri de carpen, ştejar, fag şi brazi. În afară de aceste păduri, printre ele, dealurile sunt folosite pentru agricultură şi păşunat. În anul 1427, regele Ungariei îi conferă dreptul “Opium Taro”, dar nu pentru mult timp, din cauza cetăţii Sighişoara, foarte nemulţumită de această decizie.
              Localitatea Brădeni este localizată pe hartă la 46°5'39'' nord, 24°50'39'' est. Este aşezată în partea de nord-est a judeţului Sibiu, mărginindu-se cu judeţele Mureş şi Braşov, la 19 km de oraşul Sighişoara, la 22 km de oraşul Agnita, la 35 km de oraşul Făgăraş şi la 72 km de municipiul Sibiu. Localităţile mai apropiate sunt Retiş, Ţeline, Noiştat, Netuş, Apold, Movile şi Iacobeni.
              În trecut Brădeniul făcea parte din scaunul inferior al Sighişoarei alături de multe alte localităţi. Prima atestare documentarã datează din 1297 când este menţionat cu numele de „terra Heen”. În documentele vechi din anul 1349 este amintită comuna sub numele Villa Hegun.
           </p>

            <br>
            <h6 style=" text-align:justify; line-height: 1.2";>B. Istoricul bisericii parohiale</h6>


           <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              <span style=" text-decoration:underline;">Istoria zidirii ei:</span>
           </p>

            <p style="text-align:justify; text-indent: 5ch;">
              Biserica Ortodoxă Română cu hramul “Sfântul Prooroc Ilie Tesviteanul” a fost construită în anul 1824. După tradiţie în satul Brădeni a existat o biserică de lemn aflată pe un deal din apropiere. Această biserică a fost demolată, iar materialul valorificat. Un lucru deosebit este faptul că biserica este aşezată în centrul satului, spre deosebire de localităţile vecine, unde toate bisericile ortodoxe sunt construite pe dealurile limitrofe. Situaţia aceasta se datorează faptului că românii, deşi deţineau acel teren, au obţinut dreptul de a-şi construi biserica acolo în urma unui conflict deschis cu saşii, pe atunci majoritari în localitate.
            </p>

              <p style="text-align:justify; text-indent: 5ch;">
              <span style=" text-decoration:underline;">Arhitectura (plan, dimensiuni, material de construcţie):</span>
           </p>

             <p style="text-align:justify; text-indent: 5ch;">
              Locaşul de cult este zidit din piatră, cărămidă, lemn şi acoperit cu ţiglă. Are formă de navă, lungimea de 22 m şi lăţimea de 7 m. Deasupra uşii de intrare se află turnul-clopotniţă, înalt de 17 m şi lat de 5 m. Terenul este compus din 4 bolţi semicirculare din cărămidă. Iconostasul este din zid, iar pardoseala din scândură de brad. Lucrările s-au făcut din contribuţia credincioşilor, iar nume de ctitori sau donatori de seamă nu se cunosc.
            </p>

            <p style="text-align:justify; text-indent: 5ch;">
              În anul 1882, sub îndrumarea preotului Nicolae Dobre s-a reparat biserica, legându-se de jur împrejur cu fier. Între anii 1970-1975, în urma inundaţiilor din aceea perioadă, când apa a avut o înălţime de aproximativ un  metru în interiorul bisericii, biserica a avut parte de noi reparaţii în timpul păstoririi părintelui Emil Vlad. Alte reparaţii au mai avut loc în anii 1980-1981 prin grija preotul Traian Mehedinţu, în 2004 la iniţiativa preotului Marius Aldea, când s-a retencuit din nou exteriorul sfântului locaş şi în 2014 când a fost reparat acoperişul şi a fost înlocuită integral ţigla de pe nava bisericii cu ţiglă nouă.
            </p>

           <p style="text-align:justify; text-indent: 5ch;">
              <span style=" text-decoration:underline;">Pictura (când a fost realizată, de către cine, când a fost restaurată):</span>
           </p>


            <p style="text-align:justify; text-indent: 5ch;">
              Între 2006-2007, prin grija preotului Lazăr Nicu, s-au efectuat lucrările de împodobire a bisericii cu pictură, în tehnica fresco, lucrare executată de pictorul Mihai Alecu din oraşul Sighişoara. Iconostasul a fost  pictat ulterior, în anul 2009. Tot în anul 2009 vechile stranele din biserică au fost înlocuite cu unele noi realizate din lemn de stejar, cu diferite elemente de sculptură ornamentală. De asemenea s-a schimbat şi mobilierul din interiorul lăcaşului, iar pe iconostas a fost pusă o cruce pictată. Pe data de 5 decembrie 2009 biserica a fost sfinţită pentru întâia dată de către Î.P.S. Dr. Laurenţiu Streza, Arhiepiscopul Sibiului şi Mitropolitul Ardealului, înconjurat de un sobor de preoţi, atât din localităţile vecine, cât şi din parohii anteriori ai comunităţii.
           </p>

            <p class="col-lg-12"> <span class="font-italic" >Obiecte vechi de cult:</span>turnul bisericii adăposteşte două clopote, unul din Sighişoara de la 1854 cu inscripţia “Fusa per Fridrich Lootz Schassburg”, celălalt de la Bucureşti de la fabrica Nicolae Ionescu. Vechiul chivot de lemn al bisericii, alături de alte obiecte care au aparţinut comunităţii ortodoxe din Brădeni se pot vedea la Agnita, în cadrul colecţiei de obiecte bisericeşti de pe Valea Hârtibaciului, păstrată în spaţiul muzeal din casa parohială Agnita II (strada Griviţa nr. 4).  </p>
             <p class="col-lg-12"> <span class="font-italic" >Manuscrise şi cărţi vechi:</span> Nu este cazul.</p>

             <p class="col-lg-12"> <span class="font-italic" >Pomelnice şi alte înscrieri sau inscripţii:</span>  Nu se cunosc.</p>

             <p class="col-lg-12"> <span class="font-italic" >Şirul preoţilor parohi: </span> : în legătură cu numele preoţilor care au slujit lui Dumnezeu şi oamenilor în comunitatea din Brădeni, nu se cunosc informaţii mai devreme de anul 1855. Aceştia sunt: Ioan Dobre (1855-1869); Vasile Şandru (1870); Nicolae Căpriţă (1871-1873); Nicolae Dobre (1874-1912); Andrei Moldovan (1913-1915); Nicolae Enciu (1916); Nicolae Cristea (1917); Andrei Moldovan (1918-1922); Chirion Muntean (1923); Nicolae Moldovan (1924); Ilarion Enciu (1925-1931); Chirion Muntean (1932-1933); Laurenţiu Boca (1934); Gheorghe Tohănean (1935-1952); Gheorghe Dumitrescu (1953-1960); Emil Vlad (1960-1978); Traian Mehedinţu (1978-1986); Nicolae Dragoman (1986-1995); Dorin Gavrilescu (1995-2000); Marius Aldea (2000-2005); Nicu Lazăr (2006-2013); Florin Ţânţ (2013-prezent). Trebuie menţionat faptul că pr. Andrei Moldovan a ajuns episcop al românilor ortodocşi din Statele Unite ale Americii. </p>

             <p class="col-lg-12"> <span class="font-italic" >Cântăreţi şi epitropi de seamă: </span>unul dintre cei mai longevivi cântăreţi ai parohiei a fost Ciuchină Alexie, care a dat răspunsuri la strană mai mult de cinci decenii.</p>

