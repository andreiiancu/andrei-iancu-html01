            <h6 style="line-height: 1.2";>A. Istoricul comunității parohiale</h6>



          <p class=" col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
           <span>Numele localităţii:</span>
              denumirile localităţii de-a lungul timpului au fost: Braller, Brulza, Brullza, Braller, Broller, Bruiu.
           </p>

             <p class=" text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
           <span style=" text-decoration:underline;">Prezentarea generală a localităţii (geografic, istorico-demografic, archeologic, cultural, economic)</span>
           </p>

              <p class=" text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
              Prima atestare documentară a localităţii corespunde anului 1337. În 1488 este menţionată comuna liberă „Ballor”, aparţinând scaunului Cincu. În comună existau atunci 37 gospodării locuite, 2 gospodării nelocuite, 2 săraci întreţinuţi de societate, o şcoală şi 3 preoţi. La mijlocul secolului al XIV-lea s-a construit o biserică în stil romanic de către locuitorii comunei. La mijlocul secolului al XVI-lea biserica a fost reconstruită în stil gotic, apoi fortificată şi înconjurată cu ziduri groase şi trei turle, urmele păstrându-se şi azi. În anul 1532 satul este menţionat sub numele „Broller” având 64 de gospodării. Între anii 1549 şi 1815 a existat o dispută, cu întreruperi, între satul Bruiu şi satul Gherdeal, pe baza unei decizii de la primăria Sibiu în care se decidea că locuitorii satelor respective nu aveau voie să părăsească aceste sate, în caz contrar trebuind să plătească o dare de 25 de guldeni, bani care foloseau la impozite locale. La 1600 satul a fost bântuit de ciumă, 70% din locuitori murind. Casele ciumaţilor au fost arse până la temelie, iar cei bântuiţi de ciumă au fost izgoniţi din sat într-un loc izolat. La anul 1695 sunt menţionate 24 gospodării nelocuite, fiind repopulate cu colonişti din Ţara Oltului. În perioada 1703-1707 sărăcimea Bruiului se alătură răscoalei „curuţilor”, fiind condusă de căpitanul Fekete Vasi, din părţile locului, sub conducerea căruia este asediat scaunul Cincului. În 1774 are loc răscoala condusă de Sava Dumitru, fiind asediat Cincu Mare.Sărăcimea din Bruiu şi satele vecine se alătură mişcării, solicitările lor fiind reducerea fiscalităţii împovărătoare şi impunerea de taxe patriciatului săsesc din scaunul Cincu. La 1803 s-a construit prima biserică românească în afara satului, dărâmată ulterior şi reclădită pe fundaţia de piatră între anii 1851-1854.
           </p>

            <p class=" text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
            În 1921, la reorganizarea administrativă, după Unirea Transilvaniei cu România, comuna Bruiu a făcut parte din judeţul Făgăraş, plasa Cincu Mare. În 1952 intră în regiunea Braşov, raionul Agnita. În 1968, la ultima reorganizare administrativ-teritorială, a fost trecută la judeţul Sibiu cu centrul economic în satul Bruiu.
           </p>

            <br>
            <h6 style=" text-align:justify; line-height: 1.2";>B. Istoricul bisericii parohiale</h6>


           <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              <span style=" text-decoration:underline;">Istoria zidirii ei:</span>
           </p>

            <p style="text-align:justify; text-indent: 5ch;">
              Din lipsă de documente nu putem cunoaşte începutul organizării românilor din Bruiu sub formă de parohie cu preoţi, cântăreţi. Cert este faptul că ei fiind băştinaşii acestor locuri trebuiau să aibă o organizare bisericească chiar dacă nu aveau preot şi apelau la unul din altă parte. Este ştiut că până în preajma anului 1802, cedincioşii ortodocşi din Bruiu apelau în probleme religioase la preotul din Feldioara Făgăraşului, al cărui nume a rămas necunoscut.
            </p>

             <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              Cel dintâi preot local al acestei parohii a fost Ştefan Tiut, care s-a născut în anul 1768 şi s-a preoţit în anul 1803. Ca bun român şi ortodox, ca fiu al satului, prima lui grijă a fost de a zidi o biserică pentru credincioşii satului. Bucurându-se de o totală încredere din partea credincioşilor săi, preotul a zidit o biserică la o depărtare de ¼ km de sat, unde era şi cimitirul. Această biserică mică, dar foarte frumoasă, a fost zidită din piatră şi cărămidă fără turn. Un clopot mic şi cu sunet frumos s-a cumpărat în anul 1809 şi s-a aşezat în două furci înaintea bisericii, de unde se trăgea. El s-a străduit a înzestra biserica cu cele de trebuinţă, jertfind din punga sa. La anul 1812 el a introdus în parohie registrele matricole. Preotul Ştefan Tiut a păstorit 59 ani, murind în anul 1862 la 94 ani. Al doilea preot a fost Lazăr Tiut, fiul preotului Ştefan Tiut. El a fost hitoronisit în anul 1851, servind în calitate de capelan lângă tatăl său până la moartea acestuia în anul 1879, la 52 ani. Activitatea acestui preot a fost foarte fecundă şi de ea se leagă zidirea actulalei biserici şi a şcolii confesionale. Preotul Lazăr Tiut, îndată după intrarea lui în parohie în calitate de capelan, s-a ocupat de aducerea bisericii din marginea satului în mijlocul românilor. Începând cu anul 1851 el a stâns materialul necesar pentru zidirea bisericii, contribuind cu sume mari de bani, dând un bun exemplu creştinilor săi. Cu sprijinul credincioşilor săi s-a început zidirea actualei biserici în 1851, lucrarea a durat 3 ani şi s-a încheiat în 1854.
            </p>

              <p style="text-align:justify; text-indent: 5ch;">
              <span style=" text-decoration:underline;">Arhitectura (plan, dimensiuni, material de construcţie):</span>
           </p>

             <p style="text-align:justify; text-indent: 5ch;">
              Biserica este zidită în formă de navă, în stil bizantin, cu elemente gotice specifice zonei sud transilvănene. Are o lungime de 20 m şi o lăţime de 7 m. Turnul are o înălţime egală cu lungimea bisericii şi e acoperit cu tablă galvanizată din anul 1903. Biserica este zidită din piatră, cărămidă, lemn şi ţiglă. Plafonul este drept. În turnul ei se găsesc 3 clopote cumpărate de credincioşi în anii 1809, 1870, 1920. Curtea bisericii este împrejmuită cu gard de beton armat şi grilaj de fire din anul 1931.
            </p>

           <p style="text-align:justify; text-indent: 5ch;">
              <span style=" text-decoration:underline;">Pictura (când a fost realizată, de către cine, când a fost restaurată):</span>
           </p>


            <p style="text-align:justify; text-indent: 5ch;">
              Biserica nu are picture  murală, existând doar câteva scene pe plafonul bisericii.
           </p>

