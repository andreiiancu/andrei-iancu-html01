            <h6 style="line-height: 1.2";>A. Istoricul comunității parohiale</h6>



          <p class=" text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
            Biserica Ortodoxă Română Benești are hramul"Sfântul Nicolae" și este bisercă monument istoric Clasa B ( cod LMI/2010 SB-II-m-B poziția 12325, jud Sibiu),listă întocmită de Institutul Național al Monumentelor Istorice, face parte din protopopiatul Agnita. Numele localității este Benești.
            Cuvântul Benești derivă din denumirea veche germană Bendorf, Bagendorf și Bajendorf , este o localitate pe Valea Hârtibaciului situată la 40 de km de Sibiu.
            Date  sigure cu privire la primele așezări omenești pe aceste meleaguri nu avem. Credem însă, după spusele bătrânilor că primele  înjghebări s-au pierdut, cu trecerea timpului. Singura mărturie sigură care vorbește, nu arată  începutul existenței satului Benești , aceasta mărturie este harta plasei Nocrich  care s-a găsit în anul 1921 în oficiul parohial  din localitate. Această harta specifică comunele care erau îndreptățite cu pamânt din hotarul fostei comune Androchel. Printre alte comune era trecută și comuna Benești îndreptățită la împroprietărire.Nu se mai știe unde se află această hartă, nici dacă mai există, singurul fapt   sigur este ca harta purta data anilor 1300-1400, deci ea ne vorbește de un trecut destul de întârziat. Importanța acestei harți nu constă numai în cele înșirate mai sus , ci și prin faptul că apărea numele satului prin denumirea de “comuna Benești”  fapt ce verifica spusele bătrânilor asupra originii satului.În secolul al XI-lea în această localitate au fost colonizați 30 de familii de sași. Odată cu venirea și așezarea sașilor se schimbă și denumirea satului din Benești în Bendorf. Această schimbare se datorează conducătorului sașilor pe care îl chema Ben, ei neschimbând radical numele satului, ci s-au mulțumit ca din Benești să-i spună Bendorf, adică satul lui Ben , dupa numele său. Coloniții veniți în comună s-au așezat în centrul satului dovadă avem strada numita azi “Ulița Mare”, care se deosebește de celelalte străzi prin lărgimea ei. Șederea sașilor a fost vremelnică , astăzi din tot cea fost nu mai există nici măcar ruine , iar de plecarea lor din această localitate nu se poate preciza nimic. Un recensământ din anul 1488 scrie despre sat că are 12 familii, 2 preoți și o școală.În jurul anului 1652 mai existau în Bendorf doar 3 familii săsești care ne mai putând face fața muncilor agricole sunt completate prin aducerea unor familii românești.Această hotărâre a fost luată de scaunul din Nocrich, care menționează că populația săsească a suferit epidemii și războaie.La începutul secolului al XVII-lea dispar din viața satului ultimii sași, așezarea rămânând pur românească.   Prima atestare documentară a oamenilor în aceste locuri este din anul 1391.
           </p>

             <p class=" text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
            Existența omului în această localitate de pe Valea Hârtibaciului a fost atestată prin descoperirile arheologice efectuate în hotarul satului. Cele mai vechi urme de viețuire umană încă din preistorie au fost descoperite pe raza localității. Pe malul stâng al Hârtibaciului la 2 km în amonte de sat  se află o așezare rurală a populației autohtone din provincie, identificata în cursul unei periegheze efectuate în anul 1986. Așezarea se întinde pe o suprafață de aproximativ 2 ha , iar materialul arheologic consta din ceramica provinciala ( oale, castroane, capace, fragmente de vase  dacice lucrate cu mâna și chirpici de mici dimensiuni). Materialele se afla la Muzeul Bruckental din Sibiu.
           </p>

              <p class=" text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
            Pe teritoriul satului, din epoca bronzului ( 1800  - 800 i.e.n) fară precizarea locului unde s-au gasit, provin:
           </p>

           <p  class="col-lg-12">
             <span style="font-weight: 800;" span>a.  două farfurii de bronz</span>  de la sfârșitul epocii bronzului ;
           </p>

            <p class="col-lg-12">
             <span style="font-weight: 800;" span>b. un vas de lut cenușiu:</span> din a doua epoca a fierului;
           </p>

            <p class="col-lg-12">
             <span style="font-weight: 800;" span>c.  topor din bronz si tipar:</span> manșonul este decorat cu trei nervuri. Se potrivește perfect cu tiparul executat din tuf vulcanic, perioada bronz mijlociu;
           </p>

            <p class="col-lg-12">
             <span style="font-weight: 800;" span>d. sabie din bronz turnat : </span> mânerul este nituit de lama. Lama și mânerul sunt foarte fin decorate. Ea dateaza din perioda bronz târziu, iar tehnica și calitatea aliajului dovedesc progesele metalurgiei bronzului  în Transilvania.
           </p>

            <p class="col-lg-12" style="text-indent: 2rem; text-align:justify;">
              Terenul satului este în cea mai mare parte deluros, doua lungi lanturi de dealuri  se întind de la apus spre răsărit. Ele merg aproape în toată lungimea lor la o departare de aproximativ 500 m unul de altul . Dealurile dinspre sud ce despart satul de comuna Alțina sunt domoale iar  cele dinspre nord ce despart satul de localitățile Ighiș  și Bârghiș  sunt foarte abrupte și înalte. Pe raza satului se afla cel mai înalt deal de pe Valea Hârtibaciului , numit “ Pulesca”,  de unde se poate vedea tot drumul Sibiului, al Mediașului și al Agnitei.
           </p>

           <p class="col-lg-12" style="text-indent: 2rem; text-align:justify;">
              Aici, între pădurile de <span style="font-weight:600px;"span> “ Peste Piriu”</span> și <span style="font-weight:600px;"span>“ Lacuturi”</span>  ce despart Beneștiul de Ighișul Vechi exista o ascunzătoare naturală numită <span style="font-weight:600px;">“ Groapa ai Largă”</span> .
           </p>

           <p class="col-lg-12" style="text-indent: 2rem; text-align:justify;">
              Se pare ca aici s-a ascus Vlad Țepeș  înainte de a fi prins. Garda lui Vlad Țepeș era formată din aromâni, din sudul Dunării . S-ar putea ca unii dintre ei  să se fi stabilit aici motiv pentru care există în prezent nume, cuvinte de tip aromânesc ca : Standima, Dima , Bărbulea, Papanic; interesant este numele Basarabă, rămas de pe vremea lui Negru Vodă sau Basarab.
              Satul este așezat în partea dealurilor dinspre nord, pe malul drept al Hârtibaciului. Constrânși de configurația terenului, satul se întinde de la apus spre răsărit, aproape pe o singura stradă. Totuși din aceasta strada se desprind îndepărtindu-se spre sud alte trei ulițe scurte: Ulița Mare, Ulița Srâmtă și Ulița Nouă, iar spre nord se desprinde una singura a Bisericii.
              Terenul aferent satului  este de 908 ha unde harnicii locuitori cultiva:  grâu, porumb, cartofi, ovăz, iar într-o mică măsură  se cultivă orzul și în grădini legume.
          </p>




            <br>
            <h6 style="line-height: 1.2";>B. Istoricul bisericii parohiale.</h6>
          <p style="text-indent: 5ch;
              ">
              Înainte de anul 1700  în satul Benești există o singură biserica ortodoxă din bârne. Începutul Bisericii unite în acest sat a fost cam pe la anul 1770 când o mică parte dintre locuitori au primit unirea cu Roma. În jurul anului 1812 se începe construirea în apropierea bisericii ortodoxe si o biserica unită, azi în paragină stând gata sa cadă, mărturisește prin clopotul datat din anul 1646 purtând inscripția : “Deficiente Populo Saxanico Hundor Beconi 1646”. La această biserică au slujit preoții Ioan Popovici,  Ioan Popiți, Alexandru Samoilă, Cornel Oros, Lazăr Dogariu.
            </p>

           <p style="text-indent: 5ch;">
              Biserica actuală ortodoxă a fost ridicată între anii 1800 – 1809 fiind construcție de zid edificată în locul alteia de lemn cu contribuția întregii obști însă cu o contribuție specială recunoscută, potrivit inscripției existente pe turn, perechii  Nicolae si  Paraschiva Gavrea .
              Pictura, în tehnica „fresco”a iconostasului si altarului are o vechime egala cu cea a constructiei. În anul 1904 zugravul Ioan Comșa a intervenit în mod grosolan asupra picturii acoperind stratul inițial  cu un strat   în tempera. În anul 2013comitetul parohial în frunte cu preotul paroh Mihai Todirenchi au început demersurile restauraăii picturii la valoarea și frumusețea ei inițială. În urma acestor demersuri , a fost desemnat să execute lucrările de restaurare a picturii, pictorul restaurator MIHAIL BĂRHALĂ. Lucrările de restaurare au fost finalizate, descoperindu-se numele pictorului , anul pictării si localitatea de proveniență a pictorului. Acesta este:”Zugravul Oprea din Rășinari”, iar anul pictării este 1814. Aceste date au fost descoperite pe icoana ce o reprezintă pe Sfânta Cuvioasă Paraschiva de pe fațadade miazăzi a turnului clopotniță. La data întocmirii acestei monografii se realizează pictarea în frescă  intregii biserici.
           </p>

            <p style="text-indent: 5ch;">
              Între slujitorii altarului s-au aflat preotul Moise, hirotonit în „Serbia”  menționat în anul 1733, în statistica românilor din ardeal  facută de administrația austriacă la anul 1760-1762 copiată din arhiva de răsboiu din Viena, în Benești sunt menționați 3 preoți uniți, nici o familie unită, nici un preot ortodox si 136 de familii ortodoxe, nici o biserică unită, o biserică ortodoxă, nu exista nici o casă parohială , Iosif Gavrea ( 1809 – 1846), în 1848 preot neunit era Ioan Romanov, iar mai târziu  Ioan Pananici cu un mare rol in evenimentele instorice din Transilvania ale acelui an. Alți preoți care au slijit la aceasta biserica sunt Nicolae Gavrea, nepotul lui Iosif Gravrea  și ieromonahul Iacob Lungoci, călugărit la Mănăstirea Sinaia, Nicolae Bogorin, Liviu Scrobeț, Simion Coman, Nicolae Codrea, Albu Ioan, Canciu Marian, Zegrean Alin și actualul pr. Todirenchi Mihai.Dintre epitropii de seamă vrednic de pomenire este actualul epitrop Șerban Nicolae cu un merit deosebit în realizarea lucrărilor la această biserică.
           </p>

          <br>
          <h6 class="col-lg-12 " >C. Cimitirul</h6>

            <p class="col-lg-12" style="text-indent: 5ch;">
              Cimitirul este intr-o bună stare, intreținut și îngrijit așa cum se cuvine.
            </p>

          <br>
          <h6 style="line-height: 1.2";>D. Activități culturale și filantropice în trecut:</h6>
          <h6 class="col-lg-12" style="line-height: 1.2";>Căminul cultural</h6>

          <p style="text-indent: 5ch;">
            A fost construit în anul 1948 . El funcționează  în vechea clădire a școlii din mijlocul satului. Este alcătuit dintr-o sala mare prevăzută cu tot obiectul  necesar : scena, mobilier, vesela etc. În trecut căminul avea  o bibliotecă  și o stație  radio. Biblioteca cuprindea 900 de volume cu conținut: ideologic, politic, științific și literar.
          </p>

          <h6 class="col-lg-12" style="line-height: 1.2";>Școala </h6>
          <p style="text-indent: 5ch;">
            A fost înființată în anul 1835 și funcționa în locul unde se afla  astăzi căminul cultural. Din cauza vechimii clădirii și devenind neâncăpătoare pentru copiii înscriși, s-a simțit nevoia ridicării unei noi școli . Astfel în anul 1935 s-a pus temelia construirii noii școli.Înainte de anul 1835 școala a funcționat într-o clădire lângă vechea casă parohială, din acestea două rămânând doar ruinele.Astăzi în satul Benești nu mai funcționează nici o școală, cei câțiva copii de etnie rromă frecventează cursurile la școala din comuna Alțâna, fiind transportați cu un microbuz școlar.
          </p>

          <p style="text-indent: 5ch;">
            Câteva date și informații istorice, concentrate pe viața școlară și bisericească de altă dată , au darul se reânvie comunitatea beneștenilor în anii de trecere de la veacul al XIX-lea la cel următor . Pe atunci , ca și acum centrul spiritual spre care gravita  Beneștiul era Agnita, aici aflindu-se Oficiul Protopresbiterial Ortodox. La 5 octombrie 1892 protopopul  Piso cerea parohului de la Bendorf sa dispună: “ sub grea supunere , cele de lipsa pentru începerea învățământului “ căci, se adăuga “ din contra voiu fi silit a lua cele mai drastice măsuri deoarece frația ta ai chemarea de a susține ordinea nu a o conturba prin interese si apucătorii neconvenabile “. În anul următor, 1893 la 11 noiembrie, Consistoriul anunța că a confirmat “ actul  alegerii de învățătoriu efectuit la 14 octomvrie a.c.” vizându-l pe Ioan Mera care  urma sa achite taxa legala în 8 zile. După cum se vede școala este în acest timp o preocupare constantă a bisericii, în mediul rural , aceasta din urmă ocupându-se direct de instruirea copiiilor. Atenția organismelor superioare bisericești privind “ instrucțiunea în religie “ a fost continuată și ea denotă  un interes clar pentru acest instrument sigur de propășire a românilor din Transilvania.
          </p>

           <p class="col-lg-12" style="text-indent: 5ch;">
            În anul școlar 1895-1896 , școala confesională ortodoxă de la Benești a funcționat cu un număr mare de elevi, de vreme ce oficiul parohial a solicitat aprobare pentru ocuparea prin conscurs a două posturi de învățător, luând și taxa în numele senatului școlar al comitetului parohial.
          </p>

           <p class="col-lg-12" style="text-indent: 5ch;">
            În următorul an școlar 1897 – 1898 la Benești au fost 102 elevi pentru care funcționau doi învățători, notarul local indica oficiului parohial fie reducerea unui post , fie intregirea salariilor învățătorilor din fonduri proprii așa cum a cerut “Comisiunea administrativă comitatensa“. Din Nocrich la 20 august 1896 oficiului parohial Benești i se cerea sa raporteze daca învățătorul era definitiv sau nu își ocupase postul prin conscurs, în cazul din urmă  în termen de trei zile  urma sa fie organizat concursul.
          </p>

          <p class="col-lg-12" style="text-indent: 5ch;">
            In anul 1896, beneștenii au trăit două evenimente însemnate.   “ Poporenii ” și-au dat acordul pentru alegerea la 10 martie a unui deputat mirean  în cercul Sighișoara , de care aparțineau, Zaharia Tatar, fostul deputat, renunțând la mandat.
          </p>

           <p class="col-lg-12" style="text-indent: 5ch;">
            La 29 noiembrie 1896 parohul beneștenilor Nicolae Gavrea a fost invitat la Societatea de lectură “ Andrei Șaguna” de la Sibiu, care organizase o serbare  în memoria marelui ierarh. Slujind cu credință , altarul și interesele comunității rurale, preotul Nicoale Gavrea s-a remarcat în mod deosebit atât pe plan local cât și în fața superiorilor săi. Rezultatele sale  au determinat ridicarea în ierarhia preoțească. el fiind numit la 1 septembrie 1903 administrator protopopesc al Tractului Agnita. În circulara care a purtat actul de numire , arhiepiscopul Ioan Mețianul îndemna pe ceilalți parohi sa-i dea celui în cauză “ toată onoarea și ascultarea cuvenită , și în toate afacerile cari aparțin oficului protopopesc, cătră același să vă adresați”. Preotul Gavrea a îndeplinit respectiva însărcinare pâna la 6 februarie 1906 , în locul său fiind ales de Sinodul protopresbiterial preotul Ioachim Muntean.
          </p>

          <br>
          <h6 style="line-height: 1.2";>E. Profilul actual al parohiei:</h6>

          <p style="text-indent: 5ch;">
            Bisericile se află și astăzi neschimbate din punct de vedere constructiv, dar reparații principale s-au făcut succesiv în anii 1937,1948,1966,1971, 2008-2011 când s-a consolidat de către pr. Zegrean Alin. Din 2013 și până în prezent sa restaurat pictura inițială de pictorul restaurator Mihail Birhală sub grija pr. Paroh Todirenchi Mihai și a Comisiei de Pictura  a Mitropoliei Ardealului, cu înalta binecuvantare a I. P.S. Dr. Laurențiu Streza Mitropolitul Ardealului și Arhiepiscopul Sibiului. În prezent se lucrează la pictatrea integrală a bisericii în frescă .
          </p>
          <p style="text-indent: 5ch;">
            Activităţi pastoral-misionare, culturale, editorial, filantropice, catehetice ş.a. programul catehetic naţional “Hristos împărtăşit
            copiilor”; proiectul educativ naţional “Alege şcoala!”; participarea la etapa pe protopopiat a concursurilor naţionale de miniproiecte
            cu caracter misionar-filantropic organizate anual în întreaga Patriarhie Română; sprijinirea în măsura posibilităţilor a persoanelor
            vârstnice şi a celor care se confruntă cu grave probleme sociale
          </p>

           <p style="text-indent: 5ch;">
             În urma unui recensământ realizat în anul 1972 de către pr.Nicolae Codrea, s-a ajuns la concluzia că parohia număra123 familii de români și 14 familii de rromi(cortorari). Parohia numără astăzi 68 de familii dintre care doar 26 familii sunt români, restul fiind rromi. În prezent, preot, pe seama Parohiei Benești fiind Todirenchi Mihai.
          </p>


