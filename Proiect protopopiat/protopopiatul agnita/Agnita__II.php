          <h6 style="line-height: 1.2";>D. Activități culturale și filantropice în trecut:</h6>

          <p style="text-indent: 5ch;">
            Dând curs prevederilor Legii 63/1974 privitoare la patrimoniul cultural-naţional, cu acordul autorităţilor bisericeşti şi de
            stat, în casa în care a funcţionat vechea şcoală românească din oraş (acum casă parohială) s-a înfiinţat un mic muzeu , care
            cuprinde o microcolecţie de icoane şi alte obiecte de cult (cărţi, cruci, potire). Muzeul a fost inaugurat cu prilejul conferinţei
            de orientare din luna mai 1978, în prezenţa mitropolitului Nicolae Mladin. Obiectele care fac parte din colecţia Muzeului bisericesc
            de la parohia Agnita II din Agnita sunt următoarele: 319 cărţi vechi,108 icoane pe sticlă şi 17 de icoane; alte obiecte (cruci, potire,
            antimise, între care şi un antimis din vremea păstoririi lui Dionisie Novacovici, datat 1762). Icoanele pe sticlă provin din diferitele centre
            de creaţie artistică de acest gen din Transilvania (zona Nicula, zona Maramureşului, zona Ţării Făgăraşului). De o deosebită valoare artistică
            şi istorică sunt uşile împărăteşti pictate, rămase din vechiul iconostas al bisericii Sfântul Nicolae din Agnita, precum şi un chivot pictat,
            ambele datând de la sfârşitul secolului al XVIII-le Muzeul bisericesc din Agnita, prin piesele pe care le deţine în colecţia sa, atestă într-un mod
            specific vechimea, continuitatea şi intensitatea credinţei creştine a locuitorilor din această zonă.
          </p>

