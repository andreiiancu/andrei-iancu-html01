            <h6 style="line-height: 1.2";>B. Istoricul bisericii parohiale.</h6>
            <h6 class="col-lg-12" style="line-height: 1.2";><span>Biserica Adormirea Maicii Domnului </span></h6>

            <p style="text-indent: 5ch;
              ">
            A fost construită în anul 1856 , ca biserică parohială a credincioşilor din satul Alţâna  aşa cum arată însemnările din cele mai vechi inventare ale parohiei.
            A fost ridicată pe locul altei  biserici , tot din zid, iar materialul rezultat din demolare a fost folosit la rezidirea celei noi.
            Nu se cunosc numele ctitorilor, prin tradiţie s-a păstrat ştirea că această biserică s-a ridicat numai prin contribuţia în bani şi în muncă  a întregii obşte parohiale.
            Sub păstorirea acualului preot paroh s-au realizat ample lucrari de consolidare şi reparaţii generale.
            </p>

           <p class="col-lg-12">
             <i>Arhitectura (plan, dimensiuni, material de construcţie):</i>  navă cu elemente bizantine şi gotice, 180 mp, cărămidă şi piatră.
              În anul 2013 s-a terminat lucrările de pictare în tehnică freco, an în care  a fost sfinţită de către Înaltpreasfințitul. Dr. Laurenţiu Streza, Mitropolitul Ardealului
           </p>

