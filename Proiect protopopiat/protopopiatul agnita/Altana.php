            <h6 style="line-height: 1.2";>A. Istoricul comunității parohiale</h6>



          <p class=" text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
              Numele parohiei şi protopopiatul din care provine: Parohia Ortodoxă Română Alţâna , Protopopiatul Agnita
              Prima mențiune a localității este din 1291 când a fost menționată sub numele de "Olchona", an în care știm că doi dintre membrii puternicei familii de greavi din localitate, Gerlach și Ștefan, cumpără o proprietate. Vreme de câteva secole, Alțâna a fost dominată de această familie. A fost una din cele mai mari comunități săsești din scaunul Nocrich.
              Numele Alţâna ar putea corespunde actualei localităţi “Alczen” de pe Rinul  inferior( Germania) , locul de baştină al coloniştilor aduşi  pe  aceste meleaguri sau ar deriva din “Alzo”, întemeietorul aşezării, un german colonist, sau de la un vestit meşter pe nume “ Aliza” . O legendă relatează despre venirea primilor oameni, în numar de zece care s-au aşezat aici, toţi având acelaşi rang şi poziţie socială, de unde vine numele de “ALLZEHN” în traducere : “ toţi zece”.
           </p>

            <p class=" text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
            <i>Prezentarea generală a localităţii (geografic, istorico-demografic, arheologic, cultural, economic):</i>   În anul 1349, se consemnează într-un document ca Zlagna este  alăturată scaunului Alţânei , ceea ce dovedeşte ca Alţâna, purtând denumirea de scaun, a fost în acea vreme o aşezare mare. Un recesamânt din anul 1488  stabileşte Alţâna pe locul al doilea , după Agnita cu un numar de 80 gospodării.
              Se pare că în secolul al XIII-lea fusese construită o bazilică romanică fără turn, dar în 1549, vistieria celor Șapte scaune a cheltuit 6 galbeni pentru 6 butoaie de var destinate lucrărilor întreprinse la biserica evanghelica din Alțâna, ceea ce ar însemna că bazilica a fost transformată în biserica-hală de astăzi în cursul anului următor. Cristelnița este din anul 1404 și poartă inscripția anno domini millessimo CCCCIIII tempore regis Sigismundi.
              Fortificația are două curtine care încercuiesc biserica. Nu se știe când a fost construită, dar forma circulară a zidurilor indică secolul al XV-lea. Mai existau și alte turnuri spre vest, sud și nord, acesta din urmă prăbușindu-se în 1914.
              În anul 1848 , anul revoluţiei europene a atins şi locuitorii din Alţâna, care în frunte cu preotul Tatu s-au  unit cu răsculaţii din satele vecine şi au atacat conacele din Bârghiş.
              În 1857 se  efecuează un recensământ al populaţiei care atestă în Alţâna 759 români, 708 germani şi 235 alte naţionalităţi.
              Teritoriul satului Alţâna a fost şi este traversat de o linie ferată îngustă care este consemnată în anul 1942, când satul cumpăra acţiunile societăţii anonime  ale căii ferate Sibiu – Sighişoara.

           </p>





