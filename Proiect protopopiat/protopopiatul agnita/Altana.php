              A fost construită în anul 1940 , ca biserică parohială a credincioşilor din satul Alţâna.  Ridicarea şi înfrumuseţarea acestei biserici a fost făcută în întregime din banii credincioşilor şi cu munca prestată , lucrare coordonată de preotul acelei vremi Traian Belaşcu. Biserica a fost sfinţită în anul 1943.
           </p>

