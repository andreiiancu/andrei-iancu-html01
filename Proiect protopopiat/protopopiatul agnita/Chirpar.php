            <h6 style="line-height: 1.2";>A. Istoricul comunității parohiale</h6>


                <p class="col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
           <span style=" text-decoration:underline;">Numele localității</span>
           </p>

             <p class="col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >

                <p class=" text-justify "
            style="text-align:justify;
              text-indent: 4.9ch;
           "
            >
             Numele localităţii are origine săsească: Kyrchperch românizat în Chirper apoi Chirpăr, ceea ce s-ar traduce prin „Biserica din deal” sau „Dealul bisericii”.
           </p>

           <span style=" text-indent: 5ch;text-decoration:underline;">Prezentarea generală a localităţii (geografic, istorico-demografic, archeologic, cultural, economic)</span>
           </p>

            <p class=" text-justify "
            style="text-align:justify;
              text-indent: 4.9ch;
           "
            >
              Satul Chirpăr este situat în partea de est a judeţului Sibiu, în Podişul Hârtibaciului. Teritoriul localităţii a fost locuit din vremuri imemoriale, aşezări omenesti fiind atestate încă din epoca bronzului şi a fierului. În urma săpăturilor arheologice efectuate pe teritoriul Chirpărului s-au descoperit mai multe obiecte din timpuri străvechi, care s-au depus  la muzeele arheologice din judeţul Sibiu. Prima atestare documentară a localităţii Chirpăr datează din anii 1332-1333.
           </p>


            <br>
            <h6 style=" text-align:justify; line-height: 1.2";>B. Istoricul bisericii parohiale</h6>


           <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              <span style=" text-decoration:underline;">Istoria zidirii ei:</span>
           </p>

            <p style="text-align:justify; text-indent: 5ch;">
              Biserica parohială, actuală, cu hramul “Înălţarea Domnului” a fost zidită fără turn în anul 1884 sub păstorirea preotului Simion Ţintea. Înaintea ei a existat o altă biserică din lemn  în locul numit “Piscul Popii”, biserică despre care se spune că a fost arsă.
              Actuala biserică are formă de navă şi este clădită din piatră şi cărămidă, cu tavan si pardoseală din scândură. În anul 1899 biserica a fost mărită cu 3 metri şi s-a clădit turnul în forma lui actuală, înalt de 22 metri. Biserica are o lungime de 24 metri şi o lăţime de 8 metri.
            </p>

             <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              Pînă în anul 1986 biserica a fost zugrăvită în interior, iar în intervalul 1986-1987 a fost pictată în fresco de către pictorul Ioan Căzilă din Ilimbav, în timpul păstoririi preotului Ionaş Nicolae. Pictura se păstrează în bune condiţii şi nu s-a intervenit cu nimic asupra ei.
            </p>

             <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
                Obiectele vechi de cult, manuscrise, cărţi vechi, pomelnice şi alte înscrieri sau inscripţii nu s-au păstrat întrucât în anul 1948 întreaga arhivă parohială a fost dusă la Centrul Eparhial.
            </p>


             <p class="col-lg-12"> <span class="font-italic" >Şirul preoţilor parohi: </span> :Ştefan Popovici, Ioan Popovici, Popa Mihai, Popa Nicolae (1812-1825), Simion Ţintea senior (1853-1872), Gheorghe Borzea(1873-1893), Ambrozie Marcovici (1893-1903), Ioan Holerga (1904-1920), Constantin Tipuriţă (1921-1941), Aurel Străuţ (1941-1947), Alexandru Borcea (1949-1957), Nicolae Ionaş  (1958-2000), Ioan Socaci (2001-2008), Ioan Rus (2008-prezent).</p>

             <p class="col-lg-12"> <span class="font-italic" >Cântăreţi şi epitropi de seamă: </span>Nu s-au remarcat.</p>

          <br>


          <h6 class="col-lg-12" >C. Cimitirul</h6>

            <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              cimitirul localităţii este parohial, administrat şi îngrijit de către parohie cu multă atenţie ca un bun patrimonial.
            </p>

          <br>
          <h6 style="line-height: 1.2";>D. Activități culturale și filantropice în trecut:</h6>

          <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
            în localitatea Chirpăr s-au organizat tot timpul activităţi culturale, atât la căminul cultural cât şi la Biserica parohială. Parohia nu are o “foaie” a ei şi nici alte publicaţii religioase, nu are cor, cântarea fiind omofonă, nu are muzeu propriu. Biblioteca parohială este bine organizat şi cuprinde principalele apariţii de carte şi reviste teologice.
          </p>

          <br>
          <h6 style="line-height: 1.2";>E. Profilul actual al parohiei:</h6>

           <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
            Parohia Chirpăr numără 580 de suflete, adică 162 de familii, dintre care români 55%, iar țigani 45%. Mai există 10 familii de saşi care sunt evanghelici, dar alte secte sau culte neoprotestante nu există.
          </p>

          <p style="text-align:justify; text-indent: 5ch;">
            Activităţi pastoral-misionare, culturale, editoriale, filantropice, catehetice ş.a.: Având în vedere numărul mare de rromi, care în majoritatea lor sunt săraci şi foarte săraci, desfăşurăm o amplă activitate filantropică, de sprijin material şi asistenţă duhovnicească gratuită. Pe plan pastoral–misionar desfăşurăm activitate asiduă, organizând săptămânal la biserică ore de catehizare pe clase şi pe grupe de vârstă.
          </p>

