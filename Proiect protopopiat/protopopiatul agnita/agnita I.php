<h6 style="line-height: 1.2";>D. Activităţi culturale şi filantropice:</h6>

<p style="text-indent: 5ch;">
  Corul bisericesc Sf. Ierarh Nicolae a fost înfiinţat în anul 1979.  Un moment deosebit în viața corului „Sfântul Nicolae” l-a marcat ziua de 14 noiembrie 1994 când acesta a primit atestarea de Cor Bisericesc, prin binecuvântarea Mitropolitului Ardealului , dr.Antonie Plămădeală. Creșterea valorică a acestei formații corale s-a realizat și prin contactele culturale cu formații similare; legăturile cu corurile bisericilor ortodoxe din Mediaș, Târnăveni, Sighișoara, Victoria și alte localități din zonă, vizitele reciproce au optimizat activitatea și au lărgit perspectiva repertorială a acestuia. Participarea la oficierea Sfintei Liturghii în mai multe mănăstiri (Sâmbăta de Sus, Râmeț, Recea, Cozia, Ţichindeal, Schitul Păltiniș ș.a.) a contribuit la modelarea interpretării în consonanță cu spiritualitatea ortodoxă. Din 1994 corul a participat anual la festivaluri corale de muzică religioasă, dar și laică, unde măsura valorii lui s-a concretizat în premii și aprecieri deosebite pentru calitatea interpretării. În perioada 1993 – 2007 corul „Sfântul Nicolae” a efectuat cinci turnee în Austria, regiunea Steiermark, respectiv Graz, Weiz, Hollenegg și Pillichsdorf, în apropiere de Viena; în 2007 , în Franța, a susținut concerte la Bettelainville (Lorena) și Saint- Héand (aproape de Lyon). Dirijorul corului este profesorul  Doru Paul Niţescu.
</p>

