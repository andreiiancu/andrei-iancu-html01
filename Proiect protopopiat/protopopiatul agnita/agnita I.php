            <h6 style="line-height: 1.2";>A. Istoricul comunității parohiale</h6>



          <p class=" text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >

              Parohia Ortodoxă Română Agnita I face parte din  Protopopiatul Agnita  şi are în jurisdicţie următoarele străzi : Horia , Cloşca , Crişan, Avram Iancu, 1 Decembrie, Grădinilor, Spitalului şi  Livezii.
              Geografic, Agnita este situată în partea centrală a Podişului Hârtibaciului (parte integrantă a Podişului Transilvaniei), pe cursul mediu al râului cu acelaşi nume, ce traversează întreaga zonă cunoscută sub numele de Valea Hârtibaciului.
              Numele localităţii Agnita (cu variantele maghiară-Szentagota şi germană-Agnetheln) vine de la denumirea sa în limba latină „Sancta Agatha”. Sfânta Agatha, pentru care nu putem menţiona o cronologie exactă, este cinstită ca fecioară martiră, moartă la Catania (Sicilia). A trăit probabil în secolele III-IV, fiind inclusă într-un martirologiu din anul 530 d. Hr.
              Începuturile vieţii omeneşti pe meleagurile Agnitei şi de-a lungul Văii Hârtibaciului sunt îndepărtate în timp, pe teritoriul actual al oraşului Agnita poate fi localizată o aşezare rurală romană.
              Începând cu secolul al XII-lea în Transilvania au fost colonizate mai multe grupuri de saşi.
           </p>


           <p style="text-indent: 5ch;
              ">
              Este cert faptul că pe teritoriul localităţii Agnita au fost colonizaţi saşi.                                                                                                                                                                                        iar această colonizare va marca definitiv destinul acestei aşezări sub toate aspectele.
              Atestarea documentară scrisă a Agnitei este consemnată de un act din 1280.
              În Evul Mediu Agnita, cunoaşte o anumită evoluţie datorită poziţiei sale la întretăierea drumurilor ce legau Podişul Târnavelor de Ţara Bârsei. La 24 februarie 1600 a trecut prin Agnita voieodul unirii, Mihai Viteazul (1593-1601), fapt confirmat de un document emis aici şi semnat de voievod.
              Agnita a participat activ şi la desăvârşirea statului naţional unitar român de la 1 decembrie 1918. În anul 1950, Agnita a fost declarată oraş.
            </p>


            <br>
            <h6 style="line-height: 1.2";>B. Istoricul bisericii parohiale.</h6>


           <p style="text-indent: 5ch;">Construirea unei biserici noi în centrul oraşului Agnita, de proporţiile unei catedrale, a început în anul 1939 şi s-a terminat în anul 1948. Planul construcţiei a fost întocmit de marele arhitect V. Smighelschi din Bucureşti.
              Executarea lucrărilor după planul aprobat a fost încredinţată anteprenorului Fr. Zinz din Agnita, care a construit biserica cu meşteri zidari, tâmplari şi tinichigii tot din Agnita.
              Fondurile băneşti principale au fost obţinute de la Primăria comunei  Agnita, care a atribuit parohiei ortodoxe Agnita o suprafaţă mare de pădure pentru exploatare în scopul construirii noii biserici. Fondurile obţinute din exploatarea pădurii au fost suficiente pentru ridicarea clădirii în roşu sub acoperişul de tablă, pentru tencuitul exterior şi pentru procurarea lemnului de stejar necesar la confecţionarea iconostasului şi stranelor.
              În anul 1945 protopopul Emil Păcală s-a retras la pensie, iar în locul lui a venit protopopul Marcu Chirion, care a continuat lucrarea de construire a bisericii. Acesta a ocupat funcţia de protopop şi paroh al Agnitei între anii 1945-1950. În acest timp s-a pictat biserica şi a fost confecţionat iconostasul şi stranele, cu fonduri provenite din contribuţiile credincioşilor agniţeni şi din colecte.
              Între anii 1930-1959, concomitent cu protopopii Emil Păcală, Chirion Marcu şi Vitalie Zare, în postul de paroh al parohiei Agnita II a funcţionat preotul Emil Pora.
              Biserica poartă hramul Sfinţilor Împăraţi Constantin şi Elena. Este situată în centrul oraşului, într-un loc liber, la confluenţa pârâului Groden cu râul Hârtibaciul, în imediata vecinătate cu şcoala (azi Liceul „August Treboniu Laurian”) şi Casa de Cultură. În jurul bisericii se află o zonă verde, iar în faţa ei este amenajat un parc.
           </p>

          <br>
          <h6 style="line-height: 1.2";>Arhitectura (plan, dimensiuni, material de construcţie)":</h6>


           <p style="text-indent: 5ch;">Forma clădirii este de cruce greacă, cu coloane şi multe ferestre în pereţi.
             Stilul arhitectonic este cel bizantin, fapt vizibil încă de la prima vedere prin existenţa a trei cupole, dintre care una este fixată pe locul de întretăiere a celor două braţe ale crucii.
             Materialul din care este construită constă în cărămidă, beton armat, lemn şi tablă de aluminiu.
             În interior bolta centrală este continuată în forma crucii de patru abside semicirculare sprijinite pe pereţi.
             Pardoseala este din ciment şi mozaic. În subsol are un coridor şi un loc amenajat în vederea unei instalaţii de încălzire centrală.
           </p>

          <br>
          <h6 style="line-height: 1.2";>Pictura:</h6>

<p style="text-indent: 5ch;">Pereţii în interior sunt zugrăviţi cu ornamente specific bisericeşti şi pictaţi în tehnica tempera de către pictorul profesor Nicolae Brana în anii 1946-1948.
Iconostasul este lucrat artistic din lemn de stejar sculptat de către tâmplarul Nagy Domokos în colaborare cu maestrul tâmplar din Agnita, Heinrich Barner, cu Ioan Bodea, Iosif Neuman şi sculptorul Ianoş Varga.
Stranele în număr de 92 şi pupitrele cântăreţilor sunt executate artistic din lemn de stejar în atelierele maestrului Heinrich Barner din Agnita.
Masa sfântului altar este construită din zid, în formă dreptunghiulară.
În cele două turnuri ale bisericii sunt aşezate pe socluri de stejar puternice trei clopote. Un clopot mare de 500 kg se află în turnul drept, donat de către credincioşii parohiei Agnita în 1969 la stăruinţa preotului paroh protopop Vitalie Zare şi a epitropilor Simion Hârţoagă şi Cornel Moldovan. Acest clopot a fost închinat păcii. Pe el este scris în relief cuvântul „pace”, „donat de credincioşii parohiei Agnita, paroh Zare V., epitropi Hârţoagă S., Moldovan C. Celelalte două clopote, unul de 141 kg. şi altul de 81 kg. sunt montate în turnul stâng. Acestea au fost cumpărate în anul 1969 de la parohia Evanghelică C.A. Bruiu şi apoi plătite integral de către credincioşii Laurenţiu Andrei şi soţia sa Eugenia spre pomenire.
Biserica nou construită a fost sfinţită  la 19 septembrie  1948 de către mitropolitul Nicolae Bălan, protopop fiind  Chirion Marcu şi capelan preotul Pora Emil.
De-a lungul timpului această biserică a avut nevoie şi de lucrări de restaurare cauzate de anumite fenomene naturale. Din cauza tablei arămite de calitate inferioară din care a fost confecţionat acoperişul bisericii în 1945, apa de ploaie, pătrunzând prin mai multe locuri la pereţi şi tencuieli, a degradat pictura şi bolţile laterale ale naosului. Din această cauză a fost necesară reparaţia şi vopsirea acoperişului cu miniu de plumb şi vopsea de ulei. Această lucrare a fost efectuată în vara anului 1968 cu angajaţii cooperativei „Tehnica Nouă” din Sibiu, în schimbul sumei de 12000 lei. Cu această ocazie au fost fixate mai bine crucile de pe cele trei cupole, care se clătinau la bătaia vântului, făcând astfel loc scurgerii apei de ploaie în interiorul clădirii.
În 1969 s-au recondiţionat picturile degradate de umiditate, pe suprafaţa de 20 m2 cu patru icoane deteriorate. Lucrarea de recondiţionare a picturii originale a fost încredinţată tânărului viitor pictor Hârţoagă Simeon, fiul epitropului omonim. Acesta a executat pictura în condiţii optime, imitând până la totală identitate fondul şi caracterele picturii originale. În acelaşi an parcul din jurul bisericii a fost împrejmuit în faţă pe o lungime de 120 m cu zid de prefabricate din beton armat donat de Consiliul Popular Orăşenesc Agnita. Ulterior împrejmuirea acestui parc se va face cu beton şi fier ornamentat.
Începând cu anul 2010 au fost demarate lucrările de reparaţie capitală a bisericii, urmărindu-se ca în viitorul apropiat  să fie repictată biserica . Până acum s-au schimbat toate ferestrele, s-a înlocuit în mare parte tabla de pe acoperiş cu foaie de aluminiu şi s-a montat un sistem de încalzire centrala. Cupoala mare pe exterior  a fost decopertată şi retencuită ( 2014 )
</p>

