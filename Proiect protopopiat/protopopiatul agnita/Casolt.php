            <h6 style=" text-align:justify; line-height: 1.2";>B. Istoricul bisericii parohiale</h6>


           <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              <span style=" text-decoration:underline;">Istoria zidirii ei:</span>
           </p>

            <p style="text-align:justify; text-indent: 5ch;">
              Cele mai vechi ştiri despre biserica ortodoxă din satul Caşolţ, le reproducem din cartea „Şematismul de la Blaj” apărută în anul 1900, în care, la pagina 552 se scrie: „ Biserica de piatră din anul 1879 în onoarea Cuvioasei Paraschiva. Casa parohială de piatră, din anul 1864. Şcoala din piatră, anul 1876.”
            </p>

             <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              Înainte de anul 1879 când s-a construit biserica actuală, parohia avea o bisericuţă mică din bârne de lemn, iar casa parohială din anul 1864 s-a reînnoit din temelie în anul 1905, sub păstorirea preotului Ioan Cimonea.
              Casa parohială a fost demolată în anul 1975 şi s-a construit altă casă nouă.
              Ctitoria bisericii actuale zidită în 1879 aparţine preotului Nicolae Radu. Biserica a fost zidită prin munca şi donaţia credincioşilor şi prin strădania preotului Nicolae Radu, care a păstorit între anii 1869 – 1903.
            </p>

              <p style="text-align:justify; text-indent: 5ch;">
              <span style=" text-decoration:underline;">Arhitectura (plan, dimensiuni, material de construcţie):</span>
           </p>

             <p style="text-align:justify; text-indent: 5ch;">
               Arhitectura bisericii: stil autohton – formă de navă.
            </p>

            <p style="text-align:justify; text-indent: 5ch;">
              Lucrări de reparaţii s-au făcut în anii 1966 şi 1967 prin strădania preotului Gherghel Iulian ( 1949 – 1972) – acoperişul bisericii, zugrăvire interioară şi exterioară, introducerea curentului electric, reînnoirea ferestrelor şi icoanele pe pînză de pe iconostas.
              Cele mai multe şi mai importante lucrări s-au efectuat prin grija şi preocuparea preotului Corneliu Voinea instalat în parohie în anul 1976, 01 iulie. Până la data întocmirii prezentei monografii, în cei 39 de ani de păstorire s-au efectuat următoarele lucrări:
            </p>

             <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              La casa parohială s-au continuat şi s-au finalizat lucrările de construire a unei case noi cu uşi şi geamuri termopan la parter şi din lemn de brad la etaj, duşumea de parchet de stejar. S-a construit grajd cu şură şi grup sanitar, noi.
            </p>


             <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
                La biserica parohială s-au efectuat lucrări de subzidire ( biserica avea fundaţie) de doar 20 cm ), trotuoare din dale de beton în jurul bisericii, gard despărţitor între casa parohială şi biserică, gard nou la stradă 150 ml, refacerea acoperişului la turlă şi biserică ( ţiglă nouă şi laţi noi), podirea cu cherestea de brad a podului bisericii şi turnului,retencuirea interiorului bisericii şi pictarea bisericii, lucrare începută în 1988 şi terminată în 1991. Pictura a fost realizată în frescă de către pictorul Iosif Stieger din Cristian, judeţul Sibiu.
            </p>


             <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
                Refacerea instalaţiei electrice pe două circuite în cupru, pentru prize şi pentru iluminat.Înlocuit ferestrele şi uşa principală cu geamuri şi uşă termopan.Înlocuit în totalitate duşumeaua veche cu duşumea nouă din parchet de salcâm fixat pe o şapă de beton izolată.Introdus gazul metan în biserică şi s-a realizat încălzirea cu gaz. Refăcut masa sfântului altar din marmoră în care au fost introduse sfinte moaşte cu ocazia sfinţirii bisericii de către Înaltpreasfinţitul Dr. Laurenţiu Streza, Arhiepiscopul Sibiului şi Mitropolitul Ardealului în 18 iulie 2010. A fost dotată biserica cu obiecte şi vase de cult noi, maui multe rînduri de veşminte noi, covoare şi preşuri noi.
            </p>

            <p class="col-lg-12"> <span class="font-italic" >Obiecte vechi de cult:</span> Obiecte mai importante, de valoare istorică, în biserică nu sunt.</p>

             <p class="col-lg-12"> <span class="font-italic" >Manuscrise şi cărţi vechi:</span> Nu este cazul.</p>

             <p class="col-lg-12"> <span class="font-italic" >Pomelnice şi alte înscrieri sau inscripţii:</span>  Nu se cunosc.</p>

             <p class="col-lg-12"> <span class="font-italic" >Şirul preoţilor parohi: </span> : în legătură cu numele preoţilor care au slujit lui Dumnezeu şi oamenilor în comunitatea din Brădeni, nu se cunosc informaţii mai devreme de anul 1855. Aceştia sunt: Ioan Dobre (1855-1869); Vasile Şandru (1870); Nicolae Căpriţă (1871-1873); Nicolae Dobre (1874-1912); Andrei Moldovan (1913-1915); Nicolae Enciu (1916); Nicolae Cristea (1917); Andrei Moldovan (1918-1922); Chirion Muntean (1923); Nicolae Moldovan (1924); Ilarion Enciu (1925-1931); Chirion Muntean (1932-1933); Laurenţiu Boca (1934); Gheorghe Tohănean (1935-1952); Gheorghe Dumitrescu (1953-1960); Emil Vlad (1960-1978); Traian Mehedinţu (1978-1986); Nicolae Dragoman (1986-1995); Dorin Gavrilescu (1995-2000); Marius Aldea (2000-2005); Nicu Lazăr (2006-2013); Florin Ţânţ (2013-prezent). Trebuie menţionat faptul că pr. Andrei Moldovan a ajuns episcop al românilor ortodocşi din Statele Unite ale Americii. </p>

             <p class="col-lg-12"> <span class="font-italic" >Cântăreţi şi epitropi de seamă: </span>unul dintre cei mai longevivi cântăreţi ai parohiei a fost Ciuchină Alexie, care a dat răspunsuri la strană mai mult de cinci decenii.</p>

