            <h6 style="line-height: 1.2";>A. Istoricul comunității parohiale</h6>

             <p class="col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
           <span style=" text-decoration:underline;">Prezentarea generală a localităţii (geografic, istorico-demografic, archeologic, cultural, economic)</span>
           </p>

            <p class=" text-justify "
            style="text-align:justify;
              text-indent: 4.9ch;
           "
            >
              Satul Caşolţ, sat component al comunei Roşia, Judeţul Sibiu este situat în partea de sud – vest a Podişului Hârtibaciului, la limita de sus a Depresiunii Transilvaniei. Parohia Cașolț a făcut parte din Protopopiatul Sibiu, până la  21 martie 2017, când a trecut sub jurisdicția Protopopiatului Agnita.
           </p>

            <p class=" col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
              Caşolţul este aşezat la o distanţăde 12 km faţă de municipiul Sibiu, pe o direcţie sud – est. Casele se înşiră una lângă alta, având o orientare est – vest şi nord – sud, în lungime totală de 3,5 km.
           </p>


           <p class=" col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
              Formele de relief ce afectează împrejurimile sunt puţine, dat fiind faptul că localitatea propriuzisă este aşezată în lunca râului Hârtibaciu şi anume: în partea de nord se află Dealul Viilor cu o altitudine de 485 m, prelungindu-se spre vest printr-un şes în Dealul Bisericii ce se prelungeşte orientat spre vest cu Dealul Rozoarelor de aceiaşi altitudine.
              Spre sud, satul Caşolţ este mărginit de firul râului Hârtibaciu, lunca lui şi Dealul Cucului, Coasta Arinilor şi Lazuri, ce au o orientare paralelă cu râul Hârtibaciu pe direcţia noprd – est şi sud – est.
           </p>

           <p class=" col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
              În ce priveşte localităţile cu care se învecinează satul Caşolţ sunt următoarele : spre nord se află satul Daia, la o distanţă de 3 km, cu populaţie : români şi saşi; spre nord – est, la o distanţă de 7 km, se află satul Cornăţel cu populaţie română; spre sud – est, la o distanţă de 7 km se află satul Mohu cu populaţie de naţionalitate română, iar spre vest, în linie dreaptă, peste Dealul Bisericii, Valea Caşolţului şi păşunea Bungardului, la o distanţă de 5 km se află satul Bungard cu populaţie de naţionalitate română; spre sud – est se află satul Bradu, la o distanţă de 6 km, cu populaţie de naţionalitate română şi săsească, iar spre sud – vest se află satul Veştem, la o distanţă de 7 km, cu o populaţie de naţionalitate română.
           </p>

           <p class=" col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
              Râul principal care trece prin marginea satului, pe direcţia est – vest, Hârtibaciul, are un debit de apă variabil şi are perioade în anii cu ploi abundente, când cantitatea apelor colectate de la izvor până în dreptul satului Caşolţ inundau cu precădere partea stângă pe o lăţime de 150- 200 m şi mai puţin pe partea dreaptă unde se află calea ferată şi gospodăriile cu locuinţele oamenilor. Aceasta s-a întâmplat în anii 1941, 1970 şi ultima dată în 1975, căci după aceste inundaţii, albia râului a fost regularizată, mutată mai departe de sat.
           </p>

           <p class=" col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
              Din lipsă de documente, cu greu putem străbate în trecutul mai îndepărtat al satului Caşolţ. Singurul izvor pe care ne întemeiem istoricul este tradiţia.
              După cum spun bătrânii, satul Caşolţ s-a întemeiat prin anul 1249 prin colonizarea de saşi saxoni. Se ştie că saşii au fost colonizaţi în Ardeal, prin veacul al XII- lea sub regele Ungariei Gheza al II – lea, care a domnit între anii 1141 – 1161. Deci versiunea despre întemeierea satului în anul 1249, prin colonizare de saşi este verosimilă. Coloniştii saşi au avut atunci religia romano-catolică. Din scriptele care se găsesc în arhiva actualei biserici Evanghelice, anul 1302 septembrie 25, cu ocazia alegerii unui preot romano – catolic pentru coloniştii sasi, anume: „ Petrus filius Danielis” ştim că acest sat apare sub numele de : „ Kasten – Holtz” din care derivă numirea românească: Caşolţ.
           </p>

            <p class=" col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
              Ştiri tradiţionale din aceiaşi sursă ne mai istorisesc următoarele: În anul 1339 şi 1342 un anumit „ Kommes Danyel de Kastenholtz” a fost arbitru într-un proces de hotar dintre satul Tălmaciu şi Şelimbăr. În anul 1542 satul Caşolţ a avut un proces cu satul Daia pentru un pod. În anul 1545 satul Caşolţ a avuit un alt proces cu aşa numitul Johan Kaisser pentru o moară de apă.
              În anul 1598 satul, împreună cu satul Bradu au avut un proces cu dogarii din satul Cisnădioara pentru nişte lemne. În anul 1720 în satul Caşolţ erau în total 50 de familii şi anume: 31 familii de saşi şi 19 familii de români.
           </p>

            <p class=" col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
              <span style="text-decoration:underline;">Cum au apărut românii în Caşolţ ?</span>
           </p>

            <p class=" col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
              După spusele bătrânilor, deci pe baza tradiţiei, au apărut de prin alte sate româneşti, ca păstori la vitele saşilor. În satul Caşolţ şi astăzi se spune despre o parte în care stau românii, că locuiesc „ în păstori”.
              Aşa fiind, încet şi greu, au fost tolerate unele familii să se aşeze definitiv nu „ în sat „ ci undeva mai departe, la o margine dosnică. Relaţia dintre saşi şi români era o relaţie de supremaţie, o relaţie de „ stăpân şi slugă”. Această relaţie a fost anulată după 1944. Până în 1989, românii au ajuns să fie majoritari, 2/3 faţă de saşi 1/3. După 1989, saşii au emigrat în Germania. Astăzi mai sunt în satul Caşolţ 2 persoane de etnie – saşi.
           </p>

