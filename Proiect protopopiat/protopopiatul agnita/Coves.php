            <h6 style="line-height: 1.2";>A. Istoricul comunității parohiale</h6>

             <p class="col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
           <span style=" text-decoration:underline;">Numele localității</span>
           </p>

           <p class=" text-justify "
            style="text-align:justify;
              text-indent: 4.9ch;
           "
            >
              Satul Coveş, oraşul Agnita. În diferite documente ale vremii Coveşul este menţionat cu următoarele denumiri: 1357 poss. Kuesd, 1403 villa Kwes; 1523 Kuybesch, 1549 Kewbesch; 1733 Kovesd; 1850 Koves, Kabisch; 1854 Kovesd, Kabisch, Cuiesd.  Denumirea localităţii vine din limba maghiară “koves = de piatră”.
           </p>


             <p class="col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
           <span style=" text-decoration:underline;">Prezentarea generală a localităţii (geografic, istorico-demografic, archeologic, cultural, economic)</span>
           </p>

            <p class=" text-justify "
            style="text-align:justify;
              text-indent: 4.9ch;
           "
            >
              Localitatea Coveş este situată la aproximativ 6 km spre nord-vest de oraşul Agnita, căruia îi aparţine din punct de vedere administrativ. Vatra satului este alungită circa 2 km pe valea care poartă numele localităţii. Coveşul este un “sat adunat cu structură regulată”, cu puţine ramificaţii, specific satelor româneşti din Podişul Hârtibaciului.
           </p>

            <p class=" col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
              Descoperirile arheologice din zona Agnita, cât şi izvoarele istorice, atestă existenţa multimilenară a urmelor materiale şi umane în această parte de ţară. “Pe baza resturilor fosile de faună găsite în perimetrul oraşului Agnita, cât şi a celor de floră - analize sporopolinice ale turbei din valea Coveşului, s-a putut reconstitui mediul natural în care îşi desfăşurau activitatea primii locuitori care au şlefuit piatra, au modelat lutul, au folosit metalul în îndelungatul timp, când s-au derulat culturile Criş, Turdaş, Petreşti, Coţofeni şi Sighişoara-Wietenberg” .
           </p>


           <p class=" col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
              Încă din cele mai vechi timpuri şi în Podişul Hârtibaciului a existat o viaţă socială, politică şi bisericească, adecvată condiţiilor istorice în care au trăit românii din această parte de ţară. Se ştie că în condiţiile economice şi politice ale regimului feudal maghiar, nobilimea şi patriciatul săsesc au cotropit pământul obştiilor româneşti, încât cu timpul românii au ajuns să fie iobagi pe moşiile strămoşeşti, acaparate de aceşti cotropitori străini . Aşa s-a întâmplat şi la Coveş, pământul intrând în proprietatea marilor latifundiari maghiari. Prima menţiune documentară a localităţii Coveş datează din 1357, când într-un act este menţionată “feuda Kuesd” din satul cu acelaşi nume, care este dăruită unui credincios al regelui Ludovic cel Mare (1342-1382) al Ungariei.
           </p>


            <br>
            <h6 style=" text-align:justify; line-height: 1.2";>B. Istoricul bisericii parohiale</h6>


           <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              <span style=" text-decoration:underline;">Istoria zidirii ei:</span>
           </p>

            <p style="text-align:justify; text-indent: 5ch;">
              Ridicarea unei biserici de zid, târziu, la mijlocul secolului al XIX-lea în acest sat de români iobagi este oarecum justificată de adevărurile istorice care ne spun că în acele vremuri nu aveam dreptul ca la noi acasă să ne ridicăm locaşuri de închinare de piatră sau zid în locul celor de lemn care erau mult mai uşor de distrus.
            </p>

             <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              Cu siguranţă gândul de a ridica o biserică de zid a fost pentru preotul Eftimie Roman o prioritate încă de la venirea sa la Coveş, în anul 1813. Evenimentele imediat următoare legate de anul revoluţionar 1848 au adus multe suferinţe pentru toţi românii. În astfel de împrejurări Eftimie Roman a fost nevoit să întrerupă lucrările de zidire a bisericii căci după înfrângerea trupelor imperiale a început regimul de teroare de ale cărui barbarii nu au scăpat nici românii din Coveş. Toţi bărbaţii din sat au fost pedepsiţi, bătuţi şi supuşi chinurilor. Dintre toţi, opt au fost consideraţi vinovaţi şi împuşcaţi, fără nici o sentinţă de judecată, la 11 aprilie 1849.
            </p>

              <p style="text-align:justify; text-indent: 5ch;">
              <span style=" text-decoration:underline;">Arhitectura (plan, dimensiuni, material de construcţie):</span>
           </p>

             <p style="text-align:justify; text-indent: 5ch;">
              Executarea lor s-a făcut la ieşire din sat spre Apoş, într-un loc mai înalt unde era şi o spânzurătoare pusă pentru români. Au fost împuşcaţi pentru că erau români şi au îndrăznit să ceară un drept pe seama consătenilor lor. În astfel de împrejurări istorice şi la o vârstă înaintată preotul Eftimie Roman s-a îngrijit de ridicarea bisericii, lucrare finalizată în anul 1851. A fost o jertfă deosebită ca la vârsta de 87 de ani să construieşti o biserică şi în vremuri atât de tulburi. Ctitorii sunt credincioşii din Coveş în frunte cu preotul Eftimie Roman. A fost zidită pe un loc cumpărat de la groful maghiar Maurer, pe care acesta l-a rupt din grădina iobagului său Moise Păcurariu. Suma necesară zidirii a fost donată de credincioşi, iar pentru a definitiva lucrările s-au împrumutat 200 de florini. Pentru acest împrumut fiecare credincios din parohie a trebuit să facă drept camătă o zi de lucru pe an la moşier.
            </p>


             <p class="col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
           <span style=" text-decoration:underline;">Arhitectura (plan, dimensiuni, material de construcţie)</span>
           </p>

             <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
                Biserica are formă de navă romanică cu elemente bizantine. Turla este aşezată în partea vestică deasupra intrării în biserică. Biserica are o lungime de 16 m, lăţimea de 6 m şi înălţimea iniţială de 3,5 m. Turnul este înalt de 20 m şi lat de 4,5 m. Este zidită din piatră la fundament şi din cărămidă, lemn şi ţiglă. Nu se ştie nimic despre sfinţirea ei imediat după zidire. Biserica nu este declarată monument istoric.
            </p>


            <p class="col-lg-12 text-justify "
            style="text-align:justify;
              text-indent: 5ch;
           "
            >
           <span style=" text-decoration:underline;">Pictura (când a fost realizată, de către cine, când a fost restaurată):</span>
           </p>
             <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              În timpul păstoririi preotului Ioan Jurca (1980-1989) s-au realizat reparaţii generale de consolidare a întregului edificiu bisericesc. Interiorul a fost tencuit şi drişcuit cu mortar de var, pregătindu-se pentru pictură. S-au confecţionat strane sculptate din stejar, cât şi două uşi sculptate. Pictarea integrală a interiorului bisericii în tehnica fresco de către pictorul Căzilă Ioan din Sibiu a început la 4 mai 1986 şi s-a terminat la 20 noiembrie 1987, biserica fiind sfinţită apoi la 10 iulie 1988 de către Î.P.S. Dr. Antonie Plămădeală, Mitropolitul Ardealului.
            </p>


