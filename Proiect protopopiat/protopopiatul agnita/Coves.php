            <p class="col-lg-12" style="text-align:justify; text-indent: 5ch;">
              <span style=" text-decoration:underline;">Şirul preoţilor parohi:</span>
           </p>

            <p class="col-lg-12" style=" text-align:justify; text-indent: 5ch;">
              În cronica parohială este menţionat înainte de 1813 un preot ortodox - Ioan din Şulumberch (Dealu Frumos), care era administrator parohial, dar cu siguranţă şi înainte de această dată au existat preoţi la Coveş pentru românii ortodocşi din această parohie. În anul 1733, în urma recensământului cerut de către episcopul unit Inochentie Micu, la parohia Kovesd (Coveş) este menţionat un preot ortodox, “popa Mateiu”, având o biserică (de lemn probabil) şi 45 de familii.
           </p>


            <p class="col-lg-12" style=" text-align:justify; text-indent: 5ch;">
              Menţionarea existenţei la Coveş a unui antimis datat 1751 (astăzi pierdut) întăreşte faptul că aici a existat o viaţă bisericească activă, chiar dacă parohia figura la sfârşitul secolului al XVIII-lea ca filie la Bârghiş.
           </p>


            <p class="col-lg-12" style=" text-align:justify; text-indent: 5ch;">
              În prima consemnare de preoţi ortodocşi făcută după reactivarea eparhiei în timpul episcopului Dionisie Novacovici, în protocolul cu preoţii vechi întocmit între 1763-1765 prin care se întăreau preoţii în funcţie hirotoniţi înainte de venirea sa, sunt menţionaţi “popa Ioanichie” şi “popa Ioan” cum că au luat singhelie la Coveş.
           </p>

            <p class="col-lg-12" style=" text-align:justify; text-indent: 5ch;">
              Cartea românească de învăţătură sau Cazania lui Varlam, apărută la Iaşi în 1643, exemplarul B, păstrat în colecţia Biblioteca Mitropolitană Sibiu, provenită de la Parohia Ort. Română Ruja, având coperta de lemn învelită în piele ornamentată, are o adnotare în care este menţionat la „ano 1786, 10 zile martie…popa Crăciun Tatarovici, paroh Coveşului”.  La începutul anilor 1800 Crăciun Tatarovici menţionat ca preot la Agnita.
           </p>

            <p class="col-lg-12" style=" text-align:justify; text-indent: 5ch;">
              În anul 1805 s-a realizat un nou recensământ - Conscripţia Bisericii Ortodoxe din Ardeal - care menţionează la parohia Kovesd (Coveş), Comitatul Alba, Tractul Alba Superioară, pe “preotul Ioan Bunea hirotonit în 1788 în Transilvania şi care avea 99 de familii ortodoxe şi o biserică de lemn construită în anul 1799”.  Preotul Ioan Bunea este, fără îndoială, pr. Ioan din Şulumberch (Dealu Frumos) menţionat ca administrator la parohia Coveş înainte de 1813. În ceea ce priveşte biserica de lemn construită în 1799 este, probabil, cea despre care se vorbeşte în tradiţia satului că ar fi existat în cimitirul parohial. Conscripţia de la 1805 îl menţionează pe preotul Ioan Bunea ca paroh la Dealu Frumos , dar în acelaşi timp şi la Coveş, unde era administrator.
           </p>

           <p class="col-lg-12" style=" text-align:justify; text-indent: 5ch;">
              Deci putem menţiona înainte de anul 1813 următorii preoţi ortodocşi hirotoniţi pentru parohia Coveş: “popa” Mateiu-1733; “popa” Ioanichie-1753; “popa”Ioan-1759; preotul Crăciun Tatarovici - 1786, hirotonit la Buda în 1782; preotul Ioan Bunea - administrator înainte de 1813. În anul 1813 filia Coveş devine parohie având ca preot pe Eftimie Roman. Conscripţia făcută din ordinul generalului Bulccow menţionează la parohia Kovesd (Coveş) 77 familii ortodoxe fără biserică şi fără preot. În schimb sunt menţionaţi 3 preoţi uniţi cu 3 familii, (probabil familiile lor), cu o biserică.  Probabil că au fost numiţi aceşti preoţi uniţi, dar la Coveş nu a rămas nici o urmă de uniaţie. Cele 77 de familii ortodoxe au rămas ataşate Bisericii strămoşeşti, rezistând în faţa tuturor persecuţiilor la care au fost supuşi.
           </p>

           <p class="col-lg-12" style=" text-align:justify; text-indent: 5ch;">
              Şirul preoţilor parohi: Eftimie Roman (1813-1860);  Alexandru Comşa (1860-1888); Protopopul Sabin Piso (1888-1893, administrator parohial); Gheorghe Borzea (1893-1915); Ioan Savu (1915-1929); Emilian Terchilă de la Bârghiş, administrator parohial (1929-1931); Mihail Leicu (1931-1945); Liviu Cioca (1946-1980); Ioan Jurca (1981-1990); Mihai Naicu (1990-2007); Axente-Cosmin Coşorean (2008-2015); Andrei Iancu ( 2015-prezent).
           </p>




